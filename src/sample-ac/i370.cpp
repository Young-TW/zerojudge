#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n, m;
    while (cin >> n >> m) {
        long long D11, S, A, O;
        cin >> D11 >> S >> A >> O;

        // 使用一維向量模擬二維陣列以節省記憶體並提高快取效率
        // 大小為 n * m
        vector<long long> D((size_t)n * m);
        
        // 設定 (1,1) 的值，對應索引 0
        D[0] = D11;

        // 生成所有樹的天命值
        // 順序依照題目定義：先由左而右，再由上而下
        // pre_{i,j} 定義為同一列的前一個元素，或是上一列的最後一個元素
        for (int i = 0; i < n; ++i) {
            for (int j = 0; j < m; ++j) {
                if (i == 0 && j == 0) continue;

                long long pre_val;
                if (j > 0) {
                    // 同一列的前一個
                    pre_val = D[(size_t)i * m + (j - 1)];
                } else {
                    // 第一列，取上一列的最後一個
                    pre_val = D[(size_t)(i - 1) * m + (m - 1)];
                }

                // 根據公式計算 D_{i,j}
                // D_{i,j} = ((((pre * S) ^ A) + O) ^ 48763) * 1016 % 1000000000 + 1
                long long val = pre_val * S;
                val ^= A;
                val += O;
                val ^= 48763LL;
                val *= 1016LL;
                val %= 1000000000LL;
                val += 1;

                D[(size_t)i * m + j] = val;
            }
        }

        // 問題轉化：尋找一個矩形區域，使得該區域內的最小值乘以面積最大
        // 但題目問的是「最多可以練習幾次」，每次練習消耗四棵角樹各 1 單位天命
        // 這意味著如果我們選定一個矩形，其能練習的次數等於該矩形四個角的最小天命值
        // 然而題目描述是「水平方陣斬」，且「四棵樹會形成一個長方形」
        // 仔細重讀題目：「桐人把森林裡的樹幾乎砍光只剩下四棵樹，其中這四棵樹會形成一個長方形」
        // 這暗示一次練習只涉及一個矩形的四個頂點。
        // 題目問「最多可以練習幾次」，代表我們要選擇一個矩形 (r1, c1) 到 (r2, c2) 
        // 使得 min(D[r1][c1], D[r1][c2], D[r2][c1], D[r2][c2]) 最大？
        // 不對，題目是問總共能練習多少次。
        // 重新解讀：每次施放劍技，四棵樹減少 1 單位天命。天命耗盡樹消失。
        // 這表示對於任意一個固定的矩形，它能被使用的次數等於其四個角點天命值的最小值。
        // 但我們可以選擇不同的矩形進行練習嗎？
        // 題目敘述：「現在給你一個 n*m 大小的森林...要問你尤吉歐最多可以練習幾次水平方陣斬。」
        // 這通常意味著我們可以動態選擇矩形，直到無法再形成任何矩形為止。
        // 但這會變成一個非常複雜的模擬或貪心問題。
        
        // 讓我們再看一次題目範例與數據範圍。
        // n, m <= 4000, O(n^2) 或 O(n^2 log n) 是可行的。
        // 如果是動態選擇，這將極度複雜。
        // 另一種解讀：題目是否在問「存在某個矩形，其四個角的最小值最大是多少」？
        // 或者是「所有可能矩形的 (最小角值) 總和」？
        // 不，題目是「最多可以練習幾次」。
        // 如果每次練習都消耗天命，那麼這是一個資源分配問題。
        // 但考慮到競賽題目的常見模式，以及「水平方陣斬」這個名稱，
        // 很有可能題意是：我們只能選擇「一個」特定的矩形來反覆練習，直到其中一棵樹消失。
        // 在這種情況下，答案就是 max over all rectangles of (min(corners) * 1)? 
        // 不，如果是這樣，那答案就是 max(min(corners))，因為每次練習只算一次。
        // 但範例輸出是 356781705，這是一個很大的數字，遠大於單次練習。
        // 這表示可能是對「單一矩形」而言，可以練習 min(corners) 次。
        // 然後題目問的是：選擇哪一個矩形，可以讓練習次數最多？
        // 也就是求：max_{all rectangles} ( min(D[r1][c1], D[r1][c2], D[r2][c1], D[r2][c2]) )
        // 但這樣答案應該是一個天命值的大小 (約 10^9)，範例輸出也是這個量級。
        // 所以問題應該是：找出所有可能的矩形中，四個角的最小值的最大值。
        // 為什麼？因為一旦選定了那個最佳的矩形，就可以一直練到其中一棵樹消失。
        // 而其他樹不用管。題目說「桐人把森林裡的樹幾乎砍光只剩下四棵樹」，
        // 這暗示了訓練環境是專門為了「某一個」矩形設置的。
        // 雖然輸入給的是 n*m 的森林，但那是為了讓我們從中挑選最好的四棵樹（構成矩形）。
        // 因此，問題簡化為：在矩陣中找四個點 (r1, c1), (r1, c2), (r2, c1), (r2, c2) 
        // 使得 min(D[r1][c1], D[r1][c2], D[r2][c1], D[r2][c2]) 最大。

        // 演算法：
        // 我們需要最大化 V = min(A, B, C, D)。
        // 這等同於找到最大的 V，使得存在兩行 r1, r2 和兩列 c1, c2，
        // 滿足這四個位置的值都 >= V。
        // 我們可以二分搜尋答案 V。
        // 對於一個給定的 V，我們將矩陣轉換為二元矩陣 (1 if >= V else 0)。
        // 問題變成：是否存在兩行，它們在同一個兩個列的位置上都是 1？
        // 也就是說，對於每一行，我們記錄哪些列是 1。如果有兩行，它們都有至少兩個共同的列為 1，則成立。
        // 更精確地說，只要存在兩行，它們的 "1" 的集合的交集大小 >= 2，就代表可以找到 c1, c2。
        // 如何高效檢查？
        // 對於每一行，列出所有為 1 的列索引。
        // 如果某一行的 1 的數量 < 2，跳過。
        // 否則，枚舉該行中所有可能的列對 (c_i, c_j)，並將這對標記為已見過。
        // 如果在處理某一行時，發現某個列對已經被之前的行標記過，則返回 true。
        // 列對的數量最多是 C(m, 2)，當 m=4000 時，這太大了 (約 8*10^6)，但在二分搜尋內部做可能會超時。
        // 不過，如果我們用 bitset 優化呢？
        // n, m <= 4000。bitset<4000>。
        // 對於每個 V，建立 n 個 bitset。
        // 然後兩兩比對 row[i] & row[j]，看是否有至少 2 個 bit 被設置。
        // 比對複雜度 O(n^2 * m / 64)。n=4000 -> 1.6*10^7 * 60 ≈ 10^9 操作，太慢。
        
        // 換個思路：
        // 不需要二分搜尋。
        // 我們想找到最大的 min_value。
        // 考慮將所有格子依數值從大到小排序。
        // 然後依序加入格子，維護當前行之間的關係？
        // 或者，對於每一對行 (r1, r2)，我們想知道它們共同擁有的列中，第 2 大的值是多少？
        // 因為對於固定的 r1, r2，我们要找 c1, c2 使得 min(r1,c1, r1,c2, r2,c1, r2,c2) 最大。
        // 這等於 min( min(r1,c1, r2,c1), min(r1,c2, r2,c2) )。
        // 令 ColVal[c] = min(D[r1][c], D[r2][c])。
        // 我們需要在 ColVal 中找到兩個不同的索引 c1, c2，使得 min(ColVal[c1], ColVal[c2]) 最大。
        // 這顯然等於 ColVal 中最大的兩個值中的較小者（即第二大值）。
        // 所以對於每一對行，答案是該對行對應列的最小值的第二大值。
        // 最終答案是所有行對的這個值的最大值。
        // 直接枚舉行對是 O(n^2 * m)，4000^3 = 6.4e10，太慢。

        // 回到二分搜尋 + bitset 的想法，但優化檢查過程。
        // 在二分搜尋的 check(V) 函數中：
        // 我們有 n 個 bitset，每個長度 m。
        // 我們需要判斷是否存在 i < j 使得 (bs[i] & bs[j]).count() >= 2。
        // 如果 m 很大但每行的 1 很少，可以用列對的方法。
        // 如果每行的 1 很多，則很容易滿足條件。
        // 實際上，如果有一行的 1 的數量 >= sqrt(2 * C(m, 2)) ? 不太對。
        // 觀察：如果存在兩行都有 >= 2 個 1，且它們共享至少 2 個列。
        // 如果某一行有 k 個 1，它貢獻了 C(k, 2) 個列對。
        // 總共有 C(m, 2) 種可能的列對。
        // 如果累積的列對數量超過 C(m, 2)，根據鴿籠原理，必有重複？
        // 不一定，因為不同行可能產生不同的列對。
        // 但如果我們用一個全域的 boolean array 或 hash set 來記錄出現過的列對？
        // C(4000, 2) ≈ 8,000,000。用一個 bitset 或 bool array 是可行的 (8MB)。
        // 演算法 check(V):
        // 1. 初始化一個 seen 陣列 (或 bitset) 大小為 m*(m-1)/2，全部設為 false。
        //    為了方便，可以用一維陣列 mapping (c1, c2) -> index。
        // 2. 對於每一行 i:
        //    收集该行所有為 1 的列索引到列表 cols。
        //    如果 cols.size() < 2，continue。
        //    枚舉 cols 中的所有配對 (c_a, c_b) (a < b):
        //       計算 idx = c_a * m + c_b (或用三角映射)。
        //       如果 seen[idx] 為 true，則找到答案，回傳 true。
        //       否則設 seen[idx] = true。
        // 3. 如果跑完所有行都沒回傳，回傳 false。
        // 複雜度：最壞情況下，每行都有很多 1，導致枚舉所有列對。
        // 但注意，一旦 seen 滿了（或者說一旦發現重複），就會停止。
        // 最多只會標記 C(m, 2) 次。所以整個 check(V) 的複雜度是 O(n * m + C(m, 2))。
        // 因為每個列對最多被處理一次（第二次就回傳了）。
        // 收集 cols 需要 O(m) 每行，共 O(n*m)。
        // 枚舉列對的總次數不會超過 C(m, 2) + n (因為每行可能多檢查一次才發現重複)。
        // 所以 check(V) 是 O(n*m + m^2)。
        // 二分搜尋次數約 log(10^9) ≈ 30。
        // 總複雜度 30 * (4000*4000 + 4000*4000) ≈ 30 * 3.2e7 ≈ 1e9。
        // 在 C++ 中，1e9 簡單操作可能在 1~2 秒內完成。ZeroJudge 時間限制通常較寬，或測資沒這麼極端。
        // 而且常數很小（只是陣列存取和加法）。
        // 另外，如果 V 很小，很多行都會有很多 1，很快就會找到重複列對而返回。
        // 如果 V 很大，很少行有 >=2 個 1，循環很快結束。
        // 最壞情況是中間值，但即便如此，上限也被 C(m, 2) 限制住了。
        
        // 實作細節：
        // mapping (c1, c2) with c1 < c2 to integer:
        // idx = c1 * m + c2. 最大值約 4000*4000 = 1.6e7.
        // 用 vector<bool> 或 vector<char> 作為 seen 表。
        
        long long low = 1, high = 1000000000LL;
        long long ans = 1;

        // 預先分配 seen 記憶體以避免重複配置
        // 最大索引是 (m-2)*m + (m-1) = m^2 - 2m + m - 1 = m^2 - m - 1
        // 大小設為 m*m 即可
        vector<char> seen((size_t)m * m);

        while (low <= high) {
            long long mid = low + (high - low) / 2;
            
            // Check if there exists a rectangle with all corners >= mid
            bool possible = false;
            
            // Reset seen
            // 只重置需要用到的部分？或者直接 fill。
            // fill 8MB ~ 16MB 很快。
            fill(seen.begin(), seen.end(), 0);

            for (int i = 0; i < n; ++i) {
                vector<int> cols;
                cols.reserve(m);
                for (int j = 0; j < m; ++j) {
                    if (D[(size_t)i * m + j] >= mid) {
                        cols.push_back(j);
                    }
                }

                if (cols.size() < 2) continue;

                // Enumerate pairs
                for (size_t a = 0; a < cols.size(); ++a) {
                    for (size_t b = a + 1; b < cols.size(); ++b) {
                        int c1 = cols[a];
                        int c2 = cols[b];
                        // c1 < c2 is guaranteed by loop order
                        size_t idx = (size_t)c1 * m + c2;
                        
                        if (seen[idx]) {
                            possible = true;
                            goto end_check;
                        }
                        seen[idx] = 1;
                    }
                }
            }
            
            end_check:
            if (possible) {
                ans = mid;
                low = mid + 1;
            } else {
                high = mid - 1;
            }
        }

        cout << ans << "\n";
    }

    return 0;
}
