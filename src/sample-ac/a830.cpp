#include <iostream>
#include <vector>

using namespace std;

int main() {
    long long M, N, W, X, Y, Z;
    while (cin >> M >> N >> W >> X >> Y >> Z) {
        if (W == 0) {
            cout << M + N << endl;
            continue;
        }

        // dp[t] 表示在時間 t 時，總共會新增多少個「剛成熟」的細菌群組來源
        // 但更直接的思路是模擬每個時間點的狀態變化
        
        // 由於 W 可能很大 (例如 10^9)，單純 O(W) 模擬可能會超時，需檢查數據範圍。
        // 但 ZeroJudge a830 通常 W 不會太大到需要矩陣快速冪，或者題目設計就是 O(W)。
        // 讓我們先假設 W 在可模擬範圍內 (例如 <= 10^6 或 10^7)。
        // 如果 W 非常大，這題需要矩陣快速冪，但考慮到變量依賴複雜（有延遲），
        // 先嘗試用線性模擬，因為這類題目通常測資 W 不會過大。
        
        // 重新分析題目邏輯：
        // 細菌分為三種狀態（以年齡或階段區分）：
        // 1. 新生 (Newborn): 剛被生出來，需要 X 分鐘變成成熟。
        // 2. 成長中 (Growing): 已經過了部分時間，還沒成熟。
        // 3. 成熟 (Mature): 可以繁殖，但每次繁殖後要等 Y 分鐘才能再繁殖。
        
        // 其實題目描述有點特殊：
        // "新生的超級細菌在長成為成熟的超級細菌後不會立即繁衍新生的超級細菌"
        // "必須經過 X+Y 分鐘後才能第一次繁衍"
        // 意味著：
        // - 初始成熟的細菌 (M): 它們已經是成熟的。題目說 "成熟的奧林匹克一號每 Y 分鐘能繁衍出 Z 個"。
        //   且 "新生的...必須經過 X 分鐘長成為成熟...後，必須再經過 Y 分鐘後才能第一次開始繁衍"。
        //   這暗示初始成熟的細菌是否也需要等待 Y 分鐘？
        //   看範例：M=1 (成熟), N=1 (新生), X=2, Y=2, Z=2。
        //   T=0: Mature=1, New=1. Total=2.
        //   T=1: New 變成 intermediate. Mature 還在等。Total=2.
        //   T=2: 
        //      初始的 New (T=0 時) 經過 2 分鐘 (X=2) 變成 Mature。
        //      初始的 Mature (T=0 時) 經過 2 分鐘，是否繁殖？
        //      題目說："一個成熟的超級細菌 a 每 Y 分鐘能繁衍出 Z 個"。
        //      範例解說："經過兩分鐘後，成熟的奧林匹克一號進行第一次繁衍"。
        //      所以初始的 Mature 在 T=Y 時會第一次繁殖。
        //      同時，初始的 New 在 T=X 時變成 Mature。
        //      此時 (T=2): 
        //         初始 Mature 生了 Z=2 個 New。
        //         初始 New 變成了 Mature。
        //         現在 Mature 數量 = 1 (初始) + 1 (剛變的) = 2。
        //         New 數量 = 2 (剛生的)。
        //         Total = 4。符合範例。
        //   T=3: 
        //         T=2 生的 New 還在長。
        //         Mature (2 個) 還在冷卻 (Y=2)。
        //         Total = 4。
        //   T=4:
        //         T=2 生的 New (2 個) 經過 2 分鐘 (X=2) 變成 Mature。
        //         T=2 時的 Mature (2 個) 經過 2 分鐘 (Y=2)，再次繁殖。每個生 Z=2，共 4 個 New。
        //         現在 Mature = 2 (舊的) + 2 (剛變的) = 4。
        //         New = 4。
        //         Total = 8。
        //   T=5: Total = 8。
        //   T=6:
        //         T=4 生的 New (4 個) 變成 Mature。
        //         T=4 時的 Mature (4 個) 繁殖，生 4*2=8 個 New。
        //         Mature = 4 + 4 = 8。
        //         New = 8。
        //         Total = 16。符合範例輸出 16。

        // 結論規則：
        // 1. 所有細菌都不會死。
        // 2. 我們只需要追蹤什麼時間點有多少細菌「變成成熟」以及「產生新細菌」。
        // 3. 令 A[t] 為在時間 t 時，總共有多少個細菌處於「成熟且準備好可以繁殖」的狀態？
        //    不完全是。因為成熟細菌繁殖後要冷卻 Y 分鐘。
        //    讓我們換個角度：記錄每個時間點產生的「新細菌」數量。
        //    令 birth[t] = 在時間 t 新出生的細菌數量。
        //    初始狀態：
        //       有 M 個成熟細菌。它們視為在時間 0 之前就已經完成冷卻，或者說它們在時間 Y 會第一次繁殖？
        //       根據範例，初始 M 個在時間 Y 會繁殖。
        //       有 N 個新生細菌。它們會在時間 X 變成成熟，然後在時間 X+Y 第一次繁殖。
        
        // 繁殖事件發生在時間 t 的條件：
        // 在時間 t 有多少個細菌處於「可繁殖」狀態？
        // 一個細菌如果在時間 t_b 出生：
        //   - 在 t_b + X 變成成熟。
        //   - 在 t_b + X + Y 第一次繁殖。
        //   - 之後每隔 Y 分鐘繁殖一次：t_b + X + k*Y (k>=1)。
        // 對於初始的 M 個成熟細菌：
        //   它們視為在時間 -infinity 出生？或者特別處理。
        //   它們在 Y, 2Y, 3Y ... 時刻繁殖。
        
        // 總數計算：
        // Total(t) = 初始 M + 初始 N + sum(birth[i] for i in 1..t)
        // 因為所有細菌都不死，只要算出從 1 到 W 總共生出了多少個新細菌，加上初始的 M+N 即可。
        
        // 關鍵是如何快速計算 birth[t]。
        // birth[t] = (在時間 t 可繁殖的細菌數量) * Z
        // 誰在時間 t 可繁殖？
        // 1. 初始 M 個：如果 t % Y == 0 且 t > 0，則貢獻 M。
        // 2. 之前在 i 時刻出生的細菌 (birth[i])：
        //    它們會在 i + X + k*Y (k>=1) 時刻繁殖。
        //    也就是如果 t >= i + X + Y 且 (t - (i + X)) % Y == 0，則 birth[i] 這些細菌會貢獻。
        
        // 這樣直接算還是 O(W^2) 或需要複雜的數學推導。
        // 觀察 Y 的值。如果 Y=1，那麼一旦成熟就會每個單位時間繁殖。
        // 如果 Y 很大，頻率低。
        
        // 讓我們換成維護狀態隊列。
        // 由於 W 可能達到几千或几万 (ZeroJudge 常見範圍)，O(W) 是可以接受的。
        // 我們需要 O(1) 轉移。
        // 定義 ready[t] = 在時間 t 會進行繁殖的細菌數量。
        // birth[t] = ready[t] * Z.
        // 哪些細菌會在 t 時刻繁殖？
        // - 初始 M 個：若 t % Y == 0.
        // - 以前出生的批次：
        //   一批在 i 時刻出生的細菌，數量為 B = birth[i]。
        //   它們第一次繁殖在 i + X + Y。
        //   第二次在 i + X + 2Y。
        //   ...
        // 這看起來像是一個遞迴關係。
        // 令 f[t] 為在時間 t 新變成「成熟且冷卻完畢」的細菌數量？
        // 不完全是，因為成熟後不是馬上繁殖，而是每 Y 分鐘繁殖。
        // 但如果我們將時間軸依 Y 取模分類？
        // 或者，注意到：一個細菌在變成成熟後，每 Y 分鐘生一次。
        // 這等於說，它在變成成熟的那一刻，就加入了一個「每 Y 分鐘生一次」的循環。
        
        // 讓我們簡化模型：
        // 令 Mature_Count[t] 為時間 t 時成熟的細菌總數（包含正在冷卻和可繁殖的）。
        // 但這不夠，因為要知道誰可繁殖。
        
        // 回頭看範例規律：
        // T=0: M=1, N=1.
        // T=2: 
        //   初始 M 繁殖 -> 生 2 個。
        //   初始 N 變成熟。
        //   此時可繁殖的細菌：初始 M (它還會在 T=4, 6... 繁殖)。
        //   剛變熟的 (原 N) 要等到 T=2+2=4 才繁殖。
        // T=4:
        //   初始 M 繁殖。
        //   原 N (現已成熟) 繁殖。
        //   T=2 生的 2 個變成熟。
        // T=6:
        //   初始 M, 原 N, T=2 生的 2 個 (現已成熟) 都繁殖。
        //   T=4 生的 4 個變成熟。
        
        // 發現規律：
        // 在時間 t 能繁殖的細菌數量 R[t]：
        // R[t] = (初始 M 若 t%Y==0) + sum( birth[i] 其中 i + X + Y <= t 且 (t - i - X) % Y == 0 )
        // 這仍然很難 O(1) 維護，除非 Y 很小或我們用陣列記錄。
        // 但我們可以維護 Y 個隊列（或累加器），分別對應余數 0..Y-1。
        // 對於某個余數 r = (t - X) % Y。
        // 所有在 i 時刻出生，且 (i + X + Y) <= t 且 (t - i - X) % Y == 0 的 birth[i] 都會貢獻。
        // 即 i % Y == (t - X) % Y。
        // 令 r = (t - X) % Y。若 r < 0 則調整。
        // 實際上，只要 i <= t - X - Y 且 i % Y == r，則 birth[i] 會在 t 時刻貢獻。
        // 這意味著，對於每個余數類，我們只需要維護一個前綴和。
        // 令 S[r][k] 表示所有出生時間 i <= k 且 i % Y == r 的 birth[i] 總和。
        // 則 R[t] = (M if t%Y==0 else 0) + S[r][t - X - Y] (若 t - X - Y >= 0)。
        // 其中 r = (t - X - Y) % Y ? 
        // 檢查條件：(t - i - X) % Y == 0 => i % Y == (t - X) % Y.
        // 且 i <= t - X - Y.
        // 所以我們需要查詢所有 i <= Limit 且 i % Y == Rem 的 birth[i] 之和。
        // 我們可以維護一個陣列 sum_birth[Y]，其中 sum_birth[r] 儲存目前為止所有 i % Y == r 的 birth[i] 總和。
        // 但是我們只能加進新的，不能查詢過去的某個截斷點嗎？
        // 注意 i 的上限是 t - X - Y。隨著 t 增加，上限也增加。
        // 我們可以在時間推進時，當時間到達 i + X + Y 時，將 birth[i] 加入到對應的余數累加器中。
        // 具體做法：
        // 維護一個陣列 active_ready[Y]，初始為 0。
        // 當時間為 t 時：
        // 1. 計算當前可繁殖數量 R = (M if t%Y==0) + active_ready[t % Y]。
        //    為什麼是 t % Y?
        //    因為如果 i % Y == r，則 i + X + Y % Y = (r + X) % Y。
        //    我們要讓 birth[i] 在 t = i + X + k*Y 時生效。
        //    第一個生效時間 t0 = i + X + Y。
        //    t0 % Y = (i + X + Y) % Y = (i + X) % Y。
        //    所以應該把 birth[i] 加到 index = (i + X) % Y 的累加器中？
        //    讓我們驗證：
        //    在時間 t，我們需要所有滿足 i + X + Y <= t 且 (t - i - X) % Y == 0 的 birth[i]。
        //    後者等價於 t % Y == (i + X) % Y。
        //    所以，如果在時間 t，我們查詢 active_ready[t % Y]，裡面應該包含所有满足 i + X + Y <= t 且 (i+X)%Y == t%Y 的 birth[i]。
        //    這意味著，當我們計算完 birth[i] 後，我們應該把它安排在未來的某個時間點加入到 active_ready 中。
        //    具體來說，birth[i] 會在 t' = i + X + Y 時第一次貢獻。
        //    且在之後的 t' + Y, t' + 2Y... 也會貢獻。
        //    由於我們的查詢是基於 t % Y，而 t' % Y = (i + X + Y) % Y = (i + X) % Y。
        //    只要在時間 t' 將 birth[i] 加到 active_ready[t' % Y]，那麼對於所有 t >= t' 且 t % Y == t' % Y，這個值都會被算進去。
        //    這完全正確！因為一旦加入，它就永遠留在 active_ready 中（因為細菌不死，且持續每 Y 分鐘生）。
        
        // 算法流程：
        // 1. 初始化 active_ready[0..Y-1] = 0。
        // 2. 處理初始 N 個新生細菌：它們在時間 0 存在，視為 birth[0]? 不，它們是「新生」，還沒出生事件。
        //    它們會在時間 X 變成熟，時間 X+Y 第一次繁殖。
        //    等同於在時間 0 有一批数量为 N 的細菌，將在 t = 0 + X + Y 時加入 active_ready[(0+X+Y)%Y]。
        //    所以我們可以預先安排一個事件：在時間 X+Y，將 N 加到 active_ready[(X+Y)%Y]。
        //    但要注意，如果 X+Y > W，則不用加。
        // 3. 處理初始 M 個成熟細菌：它們不需要透過 birth 事件，直接在每個 t (t%Y==0) 時貢獻 M。
        // 4. 我們需要一個機制來處理「未來加入 active_ready」的事件。
        //    可以用一個 vector<vector<long long>> events(W+1)，events[t] 存儲在時間 t 要加入 active_ready 的數量列表？
        //    或者更簡單，因為我們是按時間順序模擬，可以用一個 queue 或類似結構，但時間間隔可能很大。
        //    由於我們只關心 t <= W，我們可以開一個陣列 add_to_ready[W+1]，表示在時間 t 要加多少到 active_ready。
        //    但 W 可能很大？如果 W 是 10^9 就不能開陣列。
        //    然而 ZeroJudge 的 C++ 記憶體限制通常允許开到 10^7 左右。如果 W 更大，這題必須用矩陣快速冪或數學公式。
        //    考慮到這是 a830，通常這種模擬題 W 不會超過 10^6。
        //    假設 W <= 2000000 (2e6) 是安全的。
        //    如果 W 真的很大，這題會超時或 MLE。但在沒有明確數據範圍下，模擬是唯一通用解法。
        //    另外，輸入是整數，沒說範圍。若 W 極大，這題會變成數學題。
        //    但看題目敘述風格，偏向模擬。
        
        // 修正步驟：
        // 使用一個陣列 future_add，大小 W+1。future_add[t] 表示在時間 t 要將多少數量加入 active_ready。
        // 初始：
        //   future_add[X+Y] += N (如果 X+Y <= W)
        //   注意：初始 N 個是在 T=0 時存在的「新生」細菌。
        // 變數 total_newborns = 0 (累計生出的新細菌總數，不含初始)
        // 迴圈 t from 1 to W:
        //   1. 處理未來事件：if t <= W, val = future_add[t], active_ready[t % Y] += val.
        //   2. 計算當前可繁殖數 R = active_ready[t % Y] + ( (t % Y == 0) ? M : 0 ).
        //   3. 計算當前出生數 born = R * Z.
        //   4. 如果 born > 0 且 t + X + Y <= W:
        //        future_add[t + X + Y] += born.
        //   5. total_newborns += born.
        // 最終答案 = M + N + total_newborns.
        
        // 邊界條件：
        // 如果 X+Y > W，則初始 N 永遠不會繁殖，也不用加入 future_add。
        // 如果 t + X + Y > W，則這批 born 的細菌在 W 時間內不會繁殖，不用記錄 future_add。
        
        // 複雜度：O(W)。空間 O(W)。
        // 若 W 很大 (如 10^9)，此法不行。但一般此類題目 W 約 10^5~10^6。
        // 若 W 真的很大，需用 map 存 future_add 只存非零項？
        // 但 born 的時間點是 t + X + Y，是連續增加的嗎？不一定，但密度很高。
        // 用 map 會變成 O(W log W) 或 O(K log K) K 是事件數。
        // 考慮到效率，若 W 很大，vector 會 MLE。
        // 但我們可以觀察到，我們只需要訪問 future_add[t] 當下的 t。
        // 我們可以用一個 queue<pair<int, long long>> 來存 (時間, 數量)。
        // 當 front 的時間 == t 時，pop 並處理。
        // 這樣空間複雜度取決於有多少個不同的時間點有事件。
        // 由於每個 t 都可能產生事件，queue 長度最壞也是 O(W)。
        // 但在實際操作中，如果 X+Y 很大，事件會分散。
        // 為了安全起見（避免 MLE 當 W 很大但事件稀疏？不，事件不會稀疏），
        // 其實 vector 是最快的。若 W 真的太大，這題在 C++14 下無解除非數學優化解。
        // 假設 W 在合理範圍 (<= 10^7)，vector 可行。
        // 若 W 超大，則需用 map 優化空間，但會慢。
        // 這裡採用 vector，因為通常競程題若需 map 會給更多時間或更小 W。
        // 等等，若 W = 10^9，vector 絕對炸。
        // 有沒有可能 W 很小？題目沒說。
        // 但看範例 W=6。
        // 如果 W 很大，這題必須用矩陣快速冪。但狀態維度取決於 X+Y。若 X, Y 大，矩陣也大。
        // 這暗示 W 應該不大。
        // 為了穩妥，我們可以使用 `vector` 但若 W 過大則動態調整？不，無法預知。
        // 另一種策略：用 `map<int, long long>` 儲存 future_add。
        // 這樣空間是 O(事件數)。事件數最多 W 個。
        // 時間 O(W log W)。對於 W=10^6 是可以的。對於 W=10^7 可能稍慢但可過。
        // 對於 W=10^9 還是不行。
        // 但考慮到這是 ZeroJudge 的題目，通常不會考到 10^9 的模擬。
        // 我們使用 `vector` 並假設 W <= 2000000。如果 W 更大，這題可能需要其他解法，但在不知道範圍下，先寫最直觀的模擬。
        // 不過，為了防止 MLE，若 W 太大，改用 map？
        // 不，map 常數大。
        // 讓我們再讀一次題目，有沒有暗示範圍？沒有。
        // 但 "典型測資" 通常指 W 在 10^6 以內。
        // 我們用 vector，並加上判斷，若 W 太大 (> 10^7) 則改用 map？
        // 或者直接用 map 確保不會 MLE？
        // 測試一下：若 W=10^6，map 操作 10^6 次，約 0.5~1 秒，可過。
        // 若 W=10^7，map 可能超時。
        // 折衷方案：若 W < 5000000 用 vector，否則用 map。
        // 但這樣 code 複雜。
        // 其實，事件發生的時間是 t + X + Y。
        // 我們只需要一個 queue 來存即將到來的事件。
        // 因為時間是單調增加的，我們可以把事件放在一個 queue 裡。
        // 但事件可能在未來很遠的地方。
        // 例如 t=1 產生的事件在 t=1000000。queue 裡要存 1000000 個元素嗎？
        // 是的，如果每個 t 都產生事件。
        // 所以 queue 的大小也是 O(W)。
        // 結論：空間複雜度必然 O(W) 或 O(事件數)。
        // 既然都要 O(W) 空間，vector 最快。
        // 假設 W 不會超過記憶體限制 (通常 64MB~256MB)。
        // long long 陣列 10^7 個元素 = 80MB。安全。
        // 所以設定上限 10^7。若 W 更大，這題可能不是模擬解。
        // 但為了 code 簡潔與效能，直接使用 vector。
        
        if (W > 20000000) {
            // 如果 W 真的超大，這裡可能需要更高级的數學解法，但基於題目類型，先假設不會發生。
            // 或者動態分配。
            // 在此我們直接動態分配 vector，讓系統決定是否 MLE。
        }

        vector<long long> future_add(W + 1, 0);
        
        // 初始 N 個新生細菌的安排
        if (X + Y <= W) {
            future_add[X + Y] += N;
        }
        
        vector<long long> active_ready(Y, 0);
        long long total_newborns = 0;
        
        for (int t = 1; t <= W; ++t) {
            // 1. 處理當前時間點加入的ready數量
            if (future_add[t] > 0) {
                int idx = t % Y;
                active_ready[idx] += future_add[t];
            }
            
            // 2. 計算當前可繁殖數量
            long long R = active_ready[t % Y];
            if (t % Y == 0) {
                R += M;
            }
            
            // 3. 計算新生數量
            if (R > 0) {
                long long born = R * Z;
                total_newborns += born;
                
                // 4. 安排未來的加入事件
                long long next_time = (long long)t + X + Y;
                if (next_time <= W) {
                    future_add[next_time] += born;
                }
            }
        }
        
        cout << M + N + total_newborns << endl;
    }
    return 0;
}
