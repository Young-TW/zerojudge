#include <iostream>
#include <vector>
#include <algorithm>
#include <map>

using namespace std;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    while (cin >> n && n != 0) {
        vector<int> p(n);
        map<int, int> count;
        int zero_count = 0;

        for (int i = 0; i < n; ++i) {
            cin >> p[i];
            if (p[i] == 0) {
                zero_count++;
            } else {
                count[p[i]]++;
            }
        }

        int k;
        cin >> k;

        int max_group = 0;
        
        // 找出原本就有的最大群組人數
        for (auto const& [ticket, cnt] : count) {
            if (cnt > max_group) {
                max_group = cnt;
            }
        }

        // 嘗試將 k 張票補給各個群組，看看能否創造更大的群組
        // 對於每個現有的票號群組，如果我們能把所有持該票的人加上部分或全部的空手粉絲(k)，
        // 並且不超過該票號的總持有數（題目隱含意思應該是每張票號對應一個入場名額？
        // 重新讀題：工作人員有多餘的票卷，他想發給來到現場卻沒有票的粉絲們。
        // 工作人員想盡量讓最大群的粉絲人數增加。
        // 這裡的邏輯應該是：持相同票號的人可以組成一群。空手的人(0)可以分配工作人員手上的票(k)。
        // 如果分配給空手的人某張票號 X，他們就能加入持有 X 票的群組。
        // 但工作人員只有 k 張"多餘"的票，這些票的號碼是什麼？題目沒說。
        // 重新理解題意："工作人員有多餘的票卷... 盡量讓最大群的粉絲人數增加"
        // 這通常意味著工作人員手中的 k 張票是可以任意指定號碼的，或者說，
        // 只要湊夠同一個號碼的人数即可。但更合理的解釋是：
        // 現有的人拿著不同的票號（或沒票）。工作人員有 k 張空白票或是可以複製任何號碼的票？
        // 如果是可以任意指定號碼，那策略就是：選定一個現有的最大群組（或是某個群組），
        // 把 k 張票都變成該群組的號碼，分給那些原本沒票的人（0）。
        // 這樣該群組的人數就會變成 original_count + min(zero_count, k)。
        // 等等，還有一種可能：k 張票是特定的號碼嗎？題目說"多餘的票卷"，沒說號碼。
        // 通常這類題目的意思是，這 k 張票可以當作任何號碼來使用，用來湊群組。
        // 或者是，這 k 張票本身就是某種號碼，但因為要最大化，我們可以假設它們能完美配合最大的那個群組？
        // 不，最直觀的解法是：
        // 1. 統計每個非零票號出現的次數。
        // 2. 統計零（沒票）的人數。
        // 3. 我們可以把 k 張票分配給沒票的人，讓他們加入某個已有的群組。
        // 為了讓最大群組最大，我們應該選擇當前人數最多的那個群組（假設其票號為 T），
        // 然後把 min(zero_count, k) 張票變成 T，分給沒票的人。
        // 這樣新的最大群組人數 = max_original_count + min(zero_count, k)。
        // 
        // 但是，有沒有可能創建一個全新的群組？比如所有人都沒票，或者 k 很大？
        // 如果所有人都沒票 (count 为空)，那最大群組就是 min(n, k) ? 不對，n 個人都沒票，
        // 工作人員給 k 張票，如果 k >= n，那大家都有票了，但票號要一樣才能算一群。
        // 工作人員可以給所有人一樣的票號嗎？題目暗示"多餘的票卷"，通常指通用的或是可以指定的。
        // 如果可以指定，那當然全部給成一樣的。
        // 所以邏輯應該是：
        // 基礎最大群組 = max(count.values()) （如果沒有非零票，則為 0）
        // 可以增加的人數 = min(zero_count, k)
        // 結果 = 基礎最大群組 + 可以增加的人數
        // 
        // 讓我們用範例驗證這個邏輯：
        // 範例 1: 5人, 1 2 0 0 5, k=0
        // count: {1:1, 2:1, 5:1}, zero=2, k=0
        // max_original = 1
        // add = min(2, 0) = 0
        // result = 1. 但範例輸出是 2。
        // 
        // 啊！我理解錯了。題目說"讓最大群的粉絲先行入場"。
        // 也許"群"的定義不是"持相同票號的人"？
        // 再讀一遍："工作人員都會算好要進入會場的粉絲群人數，讓最大群的粉絲先行入場"
        // "接下來有n個數字代表第i位粉絲手上票卷號碼p... 如果粉絲手上沒有票卷，以0表示"
        // "工作人員想盡量讓最大群的粉絲人數增加"
        // 
        // 難道"群"是指連續的號碼？或是其他定義？
        // 不，看範例 1: 1 2 0 0 5, k=0 -> Output 2.
        // 如果群是指相同號碼，那最大是 1 (1, 2, 5 各一個)。為什麼是 2？
        // 難道 0 也算一種群組？有兩個 0。所以最大群組是 2 (兩個沒票的人)？
        // 如果是這樣，那當 k=0 時，最大群組就是 max(max_count_of_non_zero, zero_count)。
        // 範例 1 (k=0): zero_count = 2. max_non_zero = 1. Result = 2. 符合！
        // 
        // 範例 2: 1 2 0 0 5, k=1 -> Output 3.
        // 原本 zero=2, max_non_zero=1.
        // 現在有 k=1 張票。工作人員可以發給沒票的人。
        // 如果發給其中一個 0，讓他變成有票。變成什麼票？
        // 如果變成 1, 2, 或 5 中的一種，那該組變成 2 人。剩下的 0 還有 1 人。
        // 那最大群組是 2？但答案是 3。
        // 這表示我的模型還是錯的。
        // 
        // 重新思考：
        // 也許工作人員手上的 k 張票，可以讓原本沒票的人 (0) 加入任何一個群組？
        // 或者，工作人員可以把 k 張票發出去，讓某些人從 0 變成有票，進而合併群組？
        // 不，票號是固定的。
        // 
        // 讓我們換個角度。
        // 範例 1: 1 2 0 0 5, k=0 -> 2. (兩個 0 是一群)
        // 範例 2: 1 2 0 0 5, k=1 -> 3.
        // 怎麼得到 3？
        // 原本有 {1}, {2}, {5}, {0, 0}.
        // 如果 k=1，工作人員給一張票。這張票可以讓一個 0 變成非 0。
        // 如果變成 1，則 {1, 1_new}, {2}, {5}, {0}. Max = 2. 不對。
        // 
        // 是不是題目意思是：工作人員手上的 k 張票，是"萬能票"？
        // 或者，工作人員可以決定這 k 張票的號碼，讓它們去湊最大的群？
        // 如果 k=1，我們把這張票給一個 0，並設定號碼為 1。那就有兩個 1。
        // 還是只有 2。
        // 
        // 等等，有沒有可能"群"的定義是：只要你有票（不管號碼），你就可以入群？
        // 不，那樣範例 1 (k=0) 時，有票的人是 1, 2, 5 (共3人)，沒票是 2 人。Max 應該是 3。但輸出是 2。
        // 所以群一定是按號碼分的。
        // 
        // 那為什麼範例 2 會是 3？
        // 輸入: 1 2 0 0 5, k=1.
        // 唯一能湊到 3 的方法：
        // 原本有兩個 0。如果 k=1，能不能把這兩個 0 和某個 existing group 合併？
        // 不可能，除非 k 足夠讓兩個 0 都變成該 group 的號碼。
        // 但 k=1。
        // 
        // 讓我再仔細看範例輸入格式。
        // 範例輸入 #1
        // 5
        // 1 2 0 0 5
        // 0  <-- 這是第一筆測資的 k? 不，題目說 "接下來有一個整數 k"
        // 5  <-- 這是第二筆測資的 n?
        // 1 2 0 0 5
        // 1  <-- 第二筆的 k
        // 5
        // 1 2 0 0 5
        // 2
        // 0 <-- 結束
        // 
        // 哦！範例輸入的結構是：
        // Case 1: n=5, tickets=[1, 2, 0, 0, 5], k=0. Output: 2.
        // Case 2: n=5, tickets=[1, 2, 0, 0, 5], k=1. Output: 3.
        // Case 3: n=5, tickets=[1, 2, 0, 0, 5], k=2. Output: 5.
        // 
        // 現在邏輯清楚了：
        // Case 1 (k=0): 群組為 {1:1, 2:1, 5:1, 0:2}. 最大是 2 (來自 0).
        // Case 2 (k=1): 我們有 1 張額外票。我們可以把它給一個 0。
        //              如果我們讓這個 0 變成 1 (或其他已存在的號碼)，那該號碼群組變 2。
        //              剩下一個 0。群組大小為 2, 1, 1, 1. Max = 2. 
        //              還是得不到 3。
        // 
        // 除非... 工作人員的 k 張票，可以讓"沒票的人"直接視為"有票且與最大群組相同"？
        // 或者，題目意思是：工作人員可以把 k 張票發給沒票的人，並且**指定**這些票的號碼，
        // 使得這些人能和現有某個群組合併，**甚至**如果 k 足夠大，可以把所有 0 都轉化。
        // 但在 Case 2 (k=1) 中，只有一張票，只能轉化一個 0。
        // 怎麼湊出 3？
        // 
        // 有沒有可能，原本的 0 不算一群？
        // 如果 0 不算一群，那 Case 1 (k=0) 的最大群組是 1。但輸出是 2。
        // 所以 0 必須算一群。
        // 
        // 再想一下：會不會是工作人員手上的票，可以讓**多個**沒票的人入群？
        // 不，一張票給一個人。
        // 
        // 有沒有可能我對"群"的理解完全錯誤？
        // "讓最大群的粉絲人數增加"
        // 也許"群"是指：在數列中連續的一段？
        // 1 2 0 0 5 -> 連續非零？ 1, 2 (長度2)? 0, 0 (長度2)? 5 (長度1)?
        // 如果是這樣，Case 1 (k=0): Max(2, 2, 1) = 2. 符合。
        // Case 2 (k=1): 把一個 0 變成非零 (例如 3)。序列變 1 2 X 0 5 (X!=0).
        // 連續非零段：1, 2, X (長度3)? 0 (長度1)? 5 (長度1)? Max = 3. 符合！
        // Case 3 (k=2): 把兩個 0 都變成非零。序列變 1 2 X Y 5.
        // 連續非零段：1, 2, X, Y, 5 (長度5). Max = 5. 符合！
        // 
        // BINGO! 題目的"群"是指**連續**的粉絲，且這些粉絲都要"有票"（非零）或者都是"沒票"（零）？
        // 不，如果是"都是沒票"，那 Case 1 中 0, 0 是連續的，長度 2。
        // 但當 k>0 時，我們把 0 變成非零，是為了讓它們和旁邊的非零連在一起。
        // 所以規則是：
        // 1. 粉絲排隊順序固定（輸入順序）。
        // 2. "群"定義為連續的一段粉絲，這段粉絲手中的票號必須"有效"且"相同"？
        //    不，如果是這樣，1 2 0 0 5 中，1 和 2 不同號，不能算一群。
        //    那 Case 1 的 2 從哪來？只能是 0, 0 這一段（假設 0 也算一種狀態，連續的 0 算一群）。
        //    但如果 0 算一群，那當 k=1 時，把中間的 0 變成非零，斷開了 0 的連續性，也沒法和 1 或 2 連（因為號碼不同）。
        // 
        // 再看一次剛才的假設：
        // 假設：只要是非零，就算"有資格入群"，而"群"是指連續的"有資格"人群？
        // 也就是忽略號碼是否相同，只要不是 0，就算同一群？
        // 測試 Case 1: 1 2 0 0 5.
        // 非零段：[1, 2] (長2), [5] (長1). 零段：[0, 0] (長2).
        // 如果規則是"連續非零"算一群，"連續零"算一群。
        // 那最大是 2. 符合。
        // Case 2 (k=1): 把一個 0 變成非零。
        // 最佳策略：把第一個 0 變成非零 -> 1 2 X 0 5.
        // 非零段：[1, 2, X] (長3), [5] (長1). 零段：[0] (長1). Max = 3. 符合。
        // Case 3 (k=2): 把兩個 0 都變成非零 -> 1 2 X Y 5.
        // 非零段：[1, 2, X, Y, 5] (長5). Max = 5. 符合。
        // 
        // 這個模型完美符合所有範例！
        // 結論：
        // 題目中的"群"是指**連續**的一段粉絲。
        // 如果這段粉絲全都"有票"（非零），他們可以一起入場（不管票號是否相同？題目描述有點誤導，但根據範例推論是如此）。
        // 或者，如果這段粉絲全都"沒票"（零），他們也算一群（可能是等待領票的群組）。
        // 工作人員可以用 k 張票，將 k 個连续的或不连续的 0 變成非零。
        // 目標是讓"連續非零"的區間長度最大化。
        // 注意：原本連續的零，如果被填補，就會和兩邊的非零合併。
        // 
        // 問題轉化為：
        // 給定一個由 0 和非零組成的序列。
        // 可以將最多 k 個 0 變成非零。
        // 求最長的連續非零子序列長度。
        // 
        // 這是一個經典的 Sliding Window (雙指針) 問題。
        // 維護一個窗口 [L, R]，記錄窗口內 0 的個數 zeros。
        // 如果 zeros <= k，則擴大 R。
        // 如果 zeros > k，則移動 L 直到 zeros <= k。
        // 過程中記錄最大的窗口長度 (R - L + 1)。
        // 
        // 等等，那原本連續的 0 算不算一種"群"？
        // 在 Case 1 (k=0) 中，如果我們只找"連續非零"，那最大是 2 ([1, 2])。
        // 但還有一段 [0, 0] 長度也是 2。
        // 題目問的是"當前能進去的最大群粉絲人數"。
        // 如果 k=0，沒票的人不能進去？
        // 題目說："工作人員有多餘的票卷，他想發給來到現場卻沒有票的粉絲們... 盡量讓最大群的粉絲人數增加"
        // "當前能進去的最大群"
        // 如果 k=0，沒票的人能進去嗎？
        // 範例 1 輸出 2。序列是 1 2 0 0 5。
        // 連續非零最大是 2 (1, 2)。連續零最大是 2 (0, 0)。
        // 如果沒票的人不能進去，那答案應該是 2 (來自 1, 2)。
        // 如果有票才能進去，那 0 的人不能算"能進去"。
        // 但為什麼我會考慮 0 是一群？因為如果只算非零，那答案本来就是 2。
        // 那我的 Sliding Window 模型（只計算連續非零）在 k=0 時會給出 2。正確。
        // k=1 時，把一個 0 變非零，最長連續非零變 3。正確。
        // k=2 時，把兩個 0 變非零，最長連續非零變 5。正確。
        // 
        // 所以，問題簡化為：**將最多 k 個 0 翻轉為 1 (非零)，求最長連續 1 的長度。**
        // 至於原本連續的 0 是否算"能進去的群"？
        // 題目說"能進去"。沒票應該不能進去。所以 0 本身不能算"能進去的人數"。
        // 那為什麼我會糾結 0 是一群？因為我怕如果全序列都是 0，k=0 時答案要是 0 還是 n？
        // 如果全是 0，k=0，沒人能進去，答案應為 0。
        // 用 Sliding Window 模型：全 0，k=0 -> 窗口內不能有 0 -> 最大長度 0。正確。
        // 如果全是 0，k=n -> 全部翻轉 -> 長度 n。正確。
        // 
        // 所以模型確認：
        // 1. 將輸入轉換為二值陣列：0 保持 0，非零變 1。
        // 2. 允許將最多 k 個 0 變成 1。
        // 3. 求最長連續 1 的長度。
        // 
        // 演算法：Sliding Window
        // L = 0, zeros = 0, max_len = 0
        // for R in 0..n-1:
        //   if arr[R] == 0: zeros++
        //   while zeros > k:
        //     if arr[L] == 0: zeros--
        //     L++
        //   max_len = max(max_len, R - L + 1)
        // 
        // 複雜度 O(N)，符合 N<=10000。
        
        // 實作細節：
        // 不需要真的建立新陣列，直接用 p[i] != 0 判斷即可。

        int left = 0;
        int zeros = 0;
        int max_len = 0;

        for (int right = 0; right < n; ++right) {
            if (p[right] == 0) {
                zeros++;
            }

            while (zeros > k) {
                if (p[left] == 0) {
                    zeros--;
                }
                left++;
            }

            int current_len = right - left + 1;
            if (current_len > max_len) {
                max_len = current_len;
            }
        }

        cout << max_len << "\n";
    }

    return 0;
}
