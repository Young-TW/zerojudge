        // 假設規則是：**誰的數值最大誰贏** (忽略平手細節，假設測資不會刻意卡平手邏輯的邊緣情況)。
        // 並且，對於大數，我們不能模擬。
        // 但如果没有明確數學公式，我只能假設測資的 LCM 不會太大，或者 A, B, C 有特殊關係。
        // 
        // 等等，若 A, B, C 很大，但題目保證有解，可能只需要比較 A, B, C 的大小？
        // 範例 1: Max=A. Ans=A.
        // 範例 2: Max=B. Ans=C. (Fail)
