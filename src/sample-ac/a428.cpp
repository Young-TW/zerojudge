// dp[u][j] 表示在以 u 為根的子樹中，修改 j 個節點所能獲得的最大額外係數和
// 由於每個節點的貢獻是 K^depth，我們實際上是在最大化 sum(K^d)
// 這裡我們重新定義問題：
// R(1) = sum_{i} C[i] * (sum of K^dist(i->1 over all paths))
// 對於樹上的節點，路徑唯一。對於環上的節點，會形成無窮級數。
// 最終 R(1) = Base_R + Extra_R
// Base_R 是不修改任何節點時的 R(1)。
// 修改一個節點 i 的原先後繼為 1，會改變 i 及其所有前驅（在樹的部分）對 R(1) 的貢獻係數。
// 具體來說，如果我們將節點 u 的後繼改為 1，那麼 u 原本通過環產生的無窮級數貢獻會被切斷，
// 取而代之的是 u 直接連向 1，產生一個固定的貢獻係數 K^(depth[u])。
// 但題目允許修改任意節點的後繼為任意節點？不，題目說「更改某些基站的后继基站」，通常這類題目的隱含意思是改成連向控制站（節點 1），因為這樣能最大化貢獻。
// 如果連向其他節點，可能只是把問題轉移。最優策略通常是將環上的某個點連向 1，或者將樹上深處的點連向 1。
// 仔細分析公式：R(i) = Ci + k * sum(R(Pj))。
// 展開後 R(1) = sum_{i=1 to N} C[i] * W[i]，其中 W[i] 是從 i 到 1 的所有路徑的 K^length 之和。
// 如果圖中存在環，且環能到達 1，則 W[i] 會包含無窮級數。
// 題目結構：每個點出度為 1，所以是基環樹森林，且因為所有點都能到達 1，所以只有一個连通分量，是一個基環樹，且 1 可能在環上或不在。
// 但題目說「从任何物流基站都可将物资运往控制基站」，這意味著 1 必須在環上？不一定。如果 1 不在環上，那麼從環出發必須能走到 1。但每個點出度為 1，如果進入環就出不來了。所以如果存在環，且所有點能到 1，那麼 1 必須在環上。
// 結論：1 一定在唯一的環上。
// 證明：假設 1 不在環上。取環上一點 u，u 的後繼在環上，永遠走不到 1，矛盾。所以 1 必在環上。
// 既然 1 在環上，那麼對於環上的任意點 i，它到 1 的路徑有無限條（繞圈）。
// 設環長為 L。環上點 i 到 1 的距離（沿著邊）為 d_i (0 <= d_i < L, 若 i=1 則 d_1=0? 不，1 的後繼是 S[1]，所以 1->S[1]->...->1)。
// 讓我們重新定義距離：dist(i, 1) 為從 i 走到 1 經過的邊數。
// 對於環上的點 i，路徑長度可以是 d_i, d_i+L, d_i+2L, ...
// 所以 W[i] = K^d_i + K^(d_i+L) + K^(d_i+2L) + ... = K^d_i / (1 - K^L)。
// 對於非環上的點 i（在樹上，指向環），設它到環上某點 j 的距離為 h，則它到 1 的路徑長度為 h + (j 到 1 的路徑)。
// 所以 W[i] = K^h * W[j] = K^h * K^d_j / (1 - K^L)。
// 現在考慮修改操作。我們可以修改最多 M 個節點的後繼。
// 修改一個節點 u 的後繼為 v，會改變 u 及其子樹（前驅們）的 W 值。
// 為了最大化 R(1)，直覺上是將某些點的後繼直接改為 1。
// 如果我們將環上的一點 u 的後繼改為 1，那麼環就被打破了。
// 此時，原來環上的點不再有无窮級數貢獻，而是變成有限的樹形結構。
// 特別地，如果我們把環上某點 u 改連向 1，那麼新的結構中，1 依然在一個環上嗎？
// 原環：1 -> s1 -> s2 -> ... -> u -> ... -> 1。
// 修改 u -> 1。新環：1 -> s1 -> ... -> u -> 1。環變小了？
// 不，原來的環是 1->...->u->...->1。如果 u 原來指向 next(u)，現在指向 1。
// 那麼新路徑是 u->1。原來的環被切斷，形成一個新的環：1->...->u->1。
// 等等，1 本來就在環上。如果 u 也在環上，且 u != 1。
// 原環順序：1 -> a -> b -> ... -> u -> v -> ... -> 1。
// 修改 u -> 1。新環：1 -> a -> ... -> u -> 1。這是一個更小的環。
// 這樣做的好處是縮短了環長 L，從而增大了分母 (1-K^L)，使得 W 值變大。
// 另一種策略：將樹上的一點 u 改連向 1。這會讓 u 及其子樹的貢獻變成 K^1 * (something)?
// 不，如果 u 連向 1，則 u 到 1 的距離變為 1。W[u] = K^1 + K^(1+L') + ... ?
// 如果 u 連向 1，而 1 在環上，那麼 u 也進入了環的贡献體系。
// 其實最優策略一定是：選擇環上的一些點（或者說，通過修改操作來改變環的結構），使得最終的 R(1) 最大。
// 觀察發現，修改一個非環上的點 u 連向 1，等價於讓 u 直接貢獻 K^1 * (1 + K^L + ...) ? 不完全是。
// 讓我們參考標準解法：
// 這題的關鍵在於：最終的圖依然必須滿足每個點出度為 1，且 1 在環上（否則無法收斂或不符合題意？題目沒說修改後必須保持所有點到 1，但為了最大化，我們肯定要保持連通）。
// 實際上，修改操作最有意義的是：將環上的某個點 u 的後繼改為 1？這不會改變 1 在環上的事實，只會改變環的大小。
// 或者，將環上的某個點 u 的後繼改為另一個點，使得環消失？不可能，因為每個點出度為 1，有限點數必有環。
// 正確的思路是：我們可以通過修改操作，將原來的環「切開」，並重新連向 1，從而改變環的長度，或者將樹上的節點直接掛到 1 上。
// 但注意，題目說「控制基站的后继基站不可被修改」。所以 S[1] 不能改。
// 這意味著 1 的出邊是固定的。所以 1 一定指向 S[1]。
// 那麼環必然包含 1 和 S[1]。
// 如果我們修改環上其他點 u (u != 1) 的後繼為 1，那麼新環就是 1->S[1]->...->u->1。環長變為 dist(1, u) + 1。
// 如果我們修改多個點呢？
// 實際上，最优解的结构一定是：最終的圖中，1 所在的環是由原圖中 1 到某個點 u 的路徑，加上 u->1 這條邊組成的。
// 也就是說，我們選擇環上的一個點 u（在原圖的環上），將 u 的後繼改為 1。這樣環長變為 len(1->u) + 1。
// 但是我們最多可以改 M 次。這意味著我們可以切斷原環上的多條邊？
// 不，如果我們切斷多條邊，例如 u->1 和 v->1，那麼 u 和 v 都指向 1。
// 此時 1 的入度增加。但 1 的出度依然是 1。
// 這樣的結構中，1 依然在一個環上嗎？
// 1 -> S[1] -> ... -> u -> 1。這是一個環。
// 那 v 呢？v -> 1。v 不在這個環上，v 是指向環的樹的一部分。
// 所以，無論怎麼改，最終 1 一定在一個環上。
// 而且，由於 S[1] 固定，環的前幾段 1->S[1]->... 是固定的。
// 我們能做的，是選擇原環上的一個點 u，將 u 的後繼改為 1，從而形成一個新環。
// 或者，我們可以選擇原樹上的一個點 v，將 v 的後繼改為 1？
// 如果 v 原本不在環上，將 v 改為指向 1，那麼 v 就變成了直接連向環（1 在環上）。這會增加 v 及其子樹的貢獻係數。
// 但這算一次修改。
// 綜合起來，問題轉化為：
// 1. 找出原圖中 1 所在的環。設環上的點為 c0=1, c1, c2, ..., c(L-1)，其中 S[c_i] = c_(i+1)%L。
// 2. 對於環上的每個點 c_i (i > 0，因為 c0=1 不能改)，我們可以選擇將 c_i 的後繼改為 1。這會消耗 1 次修改機會，並將環長變為 i+1。
//    注意：如果我們修改了 c_i，那麼原環上 c_i 之後的點（c_(i+1)...）就不再位於新環上，而是變成了掛在新環上的樹。
//    並且，我們只能修改一個環上的點來「閉合」新環嗎？
//    是的，因為新環必須從 1 出發，經過 S[1], S[S[1]]... 直到某個點 u，然後 u->1。
//    由於 S[1] 固定，路徑 1->S[1]->... 是唯一確定的，直到我們遇到一個被修改的點 u。
//    所以，我們必須在原環上選擇一個點 u（或者說，決定新環在哪裡閉合），將 u 的後繼改為 1。
//    這消耗 1 次修改。
//    剩下的 M-1 次修改可以用來做什麼？
//    可以用來將其他節點（無論是原環上被甩出去的點，還是原本就在樹上的點）的後繼改為 1。
//    將一個點 v 的後繼改為 1，會使 v 的貢獻係數變為 K^1 + K^(1+L_new) + ... = K / (1 - K^L_new) ?
//    不，如果 v 指向 1，而 1 在長為 L_new 的環上，則 v 到 1 的路徑長度為 1, 1+L_new, 1+2L_new...
//    所以 W[v] = K^1 / (1 - K^L_new)。
//    而如果不改，v 的 W[v] 可能是 K^d / (1 - K^L_old) 或者其他值。
//    顯然，將 v 改為指向 1 總是能增加貢獻（因為距離變短了，且分母可能變大）。
//    所以策略是：
//    Step 1: 枚舉新環的閉合點。即枚舉原環上的點 c_i (i from 1 to L-1)，假設我們將 c_i 改為指向 1。
//            這消耗 1 次修改。新環長 L' = i+1。
//            此時，原環上 c_(i+1) ... c_(L-1) 這些點變成了樹上的點（它們指向 c_(i+2)... 最終指向 c_i? 不，c_i 改指向 1 了，所以它們指向 c_i 的路徑斷了，它們現在指向 1 的路徑是通過 c_i 嗎？
//            原環：c0->c1->...->ci->c(i+1)->...->c(L-1)->c0。
//            修改 ci -> c0 (1)。
//            新路徑：c0->...->ci->c0。這是新環。
//            那 c(i+1) 呢？S[c(i+1)] 依然是 c(i+2)。最終 c(L-1) 指向 c0? 不，原圖 S[c(L-1)] = c0。
//            等等，原環是 c0->c1->...->c(L-1)->c0。
//            如果我們修改 ci -> c0。
//            那麼 c(i+1) 的後繼還是 c(i+2)，...，c(L-1) 的後繼還是 c0。
//            所以 c(i+1) -> ... -> c(L-1) -> c0。這部分依然在環上嗎？
//            不，新環是 c0->c1->...->ci->c0。
//            c(i+1) 去哪了？c(i+1) 的後繼是 c(i+2)，最終到 c(L-1)，然後 c(L-1) 指向 c0。
//            所以 c(i+1) 依然在一個能到達 c0 的路徑上。
//            但是 c(i+1) 本身不在新環上。新環只包含 c0...ci。
//            c(i+1)...c(L-1) 變成了掛在 c0 上的樹（因為 c(L-1)->c0）。
//            所以，當我們決定新環長為 L' = i+1 時，我們消耗了 1 次修改（改 ci）。
//            剩下的 M-1 次修改，我們可以用來選擇任意 M-1 個節點（除了 1），將它們的後繼改為 1。
//            哪些節點最值得改？當然是那些「改了之後增益最大」的節點。
//            對於任意節點 u，設其原貢獻係數為 W_old[u]，新貢獻係數（若改指向 1）為 W_new[u] = K / (1 - K^L')。
//            增益 = C[u] * (W_new[u] - W_old[u])。
//            我們需要計算所有節點的增益，然後取前 M-1 大的。
//            但是，W_old[u] 依賴於原圖結構。而對於原環上被甩出去的點 c(i+1)...c(L-1)，它們的 W_old 是基於原環長 L 的。
//            對於其他樹上的點，W_old 也是基於原環長 L 的。
//            注意：當我們改變環長時，所有點的 W 值都會變！因為分母 (1-K^L) 變了。
//            所以不能簡單地預計算增益。
//            正確做法：
//            枚舉新環長 L' (由選擇哪個 ci 決定，L' = i+1, i from 1 to L-1)。
//            對於固定的 L'：
//              1. 計算新基準貢獻：假設只改 ci->1，其他不動。
//                 此時，新環上的點 c0...ci 的 W 值變為 K^d / (1-K^L')，其中 d 是到新環入口的距離？
//                 不，對於新環上的點 u，W[u] = K^dist(u, 1 in new cycle) / (1 - K^L')。
//                 對於非新環上的點 v（包括原環上被甩出去的點和原樹上的點），它們最終會走到新環上的某個點。
//                 設 v 走到新環上點 p 的距離為 h，則 W[v] = K^h * W[p]。
//                 注意：這裡的 W[p] 是新環上的值。
//              2. 計算當前總 R(1)。
//              3. 考慮額外的 M-1 次修改。我們可以選擇任意 M-1 個點（不能選 1，且 ci 已經改過了），將它們直接指向 1。
//                 如果將點 v 改為指向 1，則 v 的新 W[v] 變為 K / (1 - K^L')。
//                 而原來的 W_old_current[v] 是多少？是在當前結構下（環長 L'，且 ci 已改）的 W 值。
//                 增益 = C[v] * (K/(1-K^L') - W_old_current[v])。
//                 我們計算所有可修改點的增益，排序取前 M-1 個。
//            然後在所有枚舉中取最大值。
//            
//            複雜度：環長 L <= N。枚舉 L' 需 O(N)。每次枚舉需計算所有點的 W 值 O(N)，排序 O(N log N)。總複雜度 O(N^2 log N)。
//            N <= 100? 題目沒給範圍，但 NOI 題通常 N<=100 或 200。O(N^2 log N) 應該可過。
//            看範例 N=4。估計 N 不大。
//            
//            細節：
//            - 如何快速計算 W_old_current[v]？
//              當環長確定為 L'，且環的結構確定（c0->...->ci->c0），我們可以 BFS/DFS 計算每個點到 1 的「基礎距離」h[v]（即不走完整環的距離，走到環上為止）。
//              但注意，對於環上的點，它們的 W 值是無窮級數。
//              對於非環上的點，W[v] = K^h[v] * W[p]，其中 p 是 v 走到的環上第一個點。
//              而 W[p] = K^dist(p, 1) / (1 - K^L')。
//              所以我們可以先把環上點的 W 值算出來，然後拓撲排序（反向邊）推算樹上點的 W 值。
//            
//            - 哪些點可以被修改？
//              除了 1 以外都可以。但 ci 已經被修改了（用來形成新環），所以剩餘可修改點是 N-2 個（排除 1 和 ci）。
//              如果 M=0，則不能修改，直接輸出原 R(1)。
//              如果 M>=1，我們枚舉 ci (i from 1 to L-1)。
//              注意：原環長為 L。點編號 c0=1, c1, ..., c(L-1)。
//              S[c_i] = c_(i+1) for i<L-1, S[c_(L-1)] = c0.
//              我們枚舉 i from 1 to L-1，將 c_i 改為指向 c0。
//              新環長 L' = i+1。
//              新環上的點：c0, c1, ..., c_i。
//              原環上剩餘點：c_(i+1), ..., c_(L-1)。它們現在構成樹，指向 c0（因為 c_(L-1)->c0）。
//              
//            - 計算增益時，對於每個點 v，計算 delta[v] = C[v] * (K/(1-K^L') - current_W[v])。
//              注意：current_W[v] 是在「只修改了 ci」的情況下的 W 值。
//              如果我們再修改 v，則 v 的 W 值變為 K/(1-K^L')。
//              所以 delta 計算正確。
//              但要注意，如果 v 本身就是 ci，則不能再改（已經改過了）。我們的枚舉中 ci 是固定的，所以計算 delta 時排除 ci 和 1。
//              
//            - 特例：如果 M=0，直接計算原圖 R(1)。
//            - 如果原圖環長 L=1？即 1->1。但題目說「后继基站不能是其本身」。所以 L>=2。
//            
//            步驟總結：
//            1. 讀入數據。
//            2. 找出 1 所在的環。由於 1 必在環上，從 1 開始走直到回到 1。
//               記錄環上點序列 cycle: [c0=1, c1, c2, ..., c(L-1)]。
//            3. 如果 M==0，直接計算原圖 R(1) 並輸出。
//               原圖 R(1) 計算：
//                 環上點 c_j 的 W[c_j] = K^j / (1 - K^L) ? 
//                 注意：c0=1, dist(1,1)=0? 不，公式 R(i) = Ci + k * sum(R(Pj))。
//                 展開後 R(1) = C[1]*W[1] + ... 
//                 對於 1，路徑長度為 0? 不，R(1) 包含 C[1]，然後加上 k * (前驅的 R)。
//                 所以 W[1] 應該是 1 + K^L + K^2L + ... = 1/(1-K^L)。
//                 對於 c_j (j>0)，從 c_j 到 1 的距離是 L-j? 
//                 環：1->c1->c2->...->c(L-1)->1。
//                 c1 到 1 的距離是 L-1? 不，c1->c2->...->c(L-1)->1，共 L-1 步？
//                 不，環長 L。1->c1 (1步), c1->c2 (1步), ..., c(L-1)->1 (1步)。
//                 從 c_j 到 1 的最短路徑長度是 L-j。
//                 所以 W[c_j] = K^(L-j) + K^(L-j+L) + ... = K^(L-j) / (1-K^L)。
//                 但等一下，R(1) 的定義中，C[1] 的係數是 1。
//                 R(1) = C[1] + k * sum(R(P of 1)).
//                 如果 1 的前驅是 c(L-1)，則 R(1) = C[1] + k * R(c(L-1)).
//                 R(c(L-1)) = C[c(L-1)] + k * R(c(L-2)).
//                 ...
//                 這是一個線性方程組。
//                 對於環上點，W[c_j] = K^(dist(c_j, 1)) / (1 - K^L)。
//                 其中 dist(1, 1) = 0? 那麼 W[1] = 1/(1-K^L)。
//                 dist(c_j, 1) = L - j (如果 c_j 是第 j 個點，j=0..L-1, c0=1)。
//                 驗證：c(L-1) 到 1 距離 1。W[c(L-1)] = K^1 / (1-K^L)。
//                 R(1) = C[1]*W[1] + C[c1]*W[c1] + ... 
//                 這似乎正確。
//                 
//                 對於樹上的點（非環），用 DFS/BFS 計算它們到環的距離 h，然後 W[u] = K^h * W[cycle_node]。
//                 
//            4. 枚舉 i from 1 to L-1 (表示將 c_i 改為指向 1)：
//               a. 新環長 L' = i+1。
//               b. 新環上的點：c0, c1, ..., c_i。
//               c. 計算新結構下所有點的 W_current[u]：
//                  - 環上點 c_j (0<=j<=i): dist = (j==0 ? 0 : i-j+1)? 
//                    新環：1->c1->...->c_i->1。
//                    c_j 到 1 的距離：
//                      j=0: 0
//                      j>0: i - j + 1? 
//                      例如 i=1: 1->c1->1。c1 到 1 距離 1。公式 i-j+1 = 1-1+1=1。正確。
//                      例如 i=2: 1->c1->c2->1。
//                         c1 到 1: c1->c2->1 (2步)。公式 2-1+1=2。正確。
//                         c2 到 1: 1步。公式 2-2+1=1。正確。
//                    所以 dist(c_j, 1) = (j==0 ? 0 : i - j + 1)。
//                    W[c_j] = K^dist / (1 - K^L')。
//                  - 非環點：包括原環上 c_(i+1)...c_(L-1) 以及原樹上的點。
//                    它們的結構不變，只是最終匯入的環變了。
//                    原環上 c_(i+1) 的後繼是 c_(i+2)，...，c_(L-1) 的後繼是 1 (c0)。
//                    所以 c_(L-1) 直接指向 1（在新環上）。
//                    c_(L-2) 指向 c_(L-1)，距離 1 步到 c_(L-1)，然後 c_(L-1) 到 1 距離 1。
//                    所以我們可以重新建圖（反向邊），從新環上的點開始 BFS，計算所有點的 W 值。
//               d. 計算當前總和 base_R = sum(C[u] * W_current[u])。
//               e. 計算每個可修改點 v (v != 1, v != c_i) 的增益：
//                  delta[v] = C[v] * (K / (1 - K^L') - W_current[v])。
//                  注意：如果將 v 改為指向 1，則 v 的新距離為 1，W_new = K^1 / (1-K^L')。
//               f. 將 delta 排序，取前 min(M-1, count) 個正增益加到 base_R。
//               g. 更新全局最大值。
//               
//            5. 輸出最大值。
//            
//            注意：K 是 double。精確度要求小數點後兩位。
//            數據範圍：N, M。題目未給，假設 N<=100, M<=N。
//            
//            實現細節：
//            - 建反向圖 rev_adj，用於從環向外傳播 W 值。
//            - 每次枚舉 i 時，重新計算 W_current。
//            - 注意 K^L' 可能非常小，但 K<1，所以沒事。
//            
//            邊界情況：
//            - M=0：直接算原圖。
//            - L=2：環只有 1 和 c1。枚舉 i=1（唯一選擇）。
//            
//            讓我們開始寫代碼。

int main() {
    // 優化 IO
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    if (!(cin >> N >> M >> K)) return 0;

    for (int i = 1; i <= N; ++i) {
        cin >> S[i];
    }
    for (int i = 1; i <= N; ++i) {
        cin >> C[i];
    }

    // 找出 1 所在的環
    // 因為 1 必在環上，直接模擬走
    vector<int> cycle;
    int cur = 1;
    // 用 visited 陣列防止死循環，但理論上走到 1 就停
    // 為了安全，最多走 N+1 步
    vector<bool> vis_path(N + 1, false);
    while (!vis_path[cur]) {
        vis_path[cur] = true;
        cycle.push_back(cur);
        cur = S[cur];
    }
    // 現在 cur 是第一個重複出現的點，因為從 1 開始，所以 cur 一定是 1
    // 檢查一下
    if (cur != 1) {
        // 理論上不會發生，因為題目保證所有點能到 1，且 1 出度 1，所以 1 必在環上
        // 但如果輸入有問題？假設輸入合法
        // 如果 cur != 1，說明 1 不在環上？這與題目矛盾。
        // 我們假設輸入合法，cycle 就是從 1 開始回到 1 之前的序列
        // 上面的迴圈會在 cur=1 時停止嗎？
        // 第一次 cur=1, vis_path[1]=true, push 1, cur=S[1].
        // ...
        // 當 cur 再次變為 1 時，vis_path[1] 已經是 true，迴圈結束。
        // 所以 cycle 包含 [1, c1, c2, ..., c(L-1)]。正確。
    }

    int L = cycle.size();
    // cycle[0] = 1, cycle[1] = c1, ..., cycle[L-1] = c(L-1)

    // 建反向圖，用於計算 W 值
    vector<vector<int>> rev_adj(N + 1);
    for (int i = 1; i <= N; ++i) {
        rev_adj[S[i]].push_back(i);
    }

    // 函數：計算給定環結構下的 R(1)
    // 參數：new_cycle_nodes: 新環上的點列表 (從 1 開始，按順序)
    //       new_cycle_len: 新環長
    //       modified_node: 已經被修改指向 1 的點（即新環的最後一個點，它原本指向別處，現在指向 1）
    //       實際上，我們只需要知道新環的點和長度，以及哪些點的後繼被改變了。
    //       但在我們的枚舉中，我們只改變了一個點 c_i -> 1。
    //       其他點的後繼保持不變。
    //       所以我們可以根據 new_cycle_nodes 來確定哪些點在環上，然後用 BFS 計算所有點的 W。
    
    auto calculate_R = [&](const vector<int>& new_cycle, int new_L, int extra_moves) -> double {
        // new_cycle: [1, c1, c2, ..., c_k] 其中 c_k 指向 1
        // 新環上的點
        vector<bool> on_cycle(N + 1, false);
        for (int u : new_cycle) on_cycle[u] = true;

        // 計算環上點的 W 值
        vector<double> W(N + 1, 0.0);
        double denom = 1.0 - pow(K, new_L);
        
        // 環上點 c_j (index j in new_cycle)
        // new_cycle[0] = 1, dist=0
        // new_cycle[j] (j>0): dist = new_L - j? 
        // 新環：new_cycle[0] -> new_cycle[1] -> ... -> new_cycle[new_L-1] -> new_cycle[0]
        // 從 new_cycle[j] 到 new_cycle[0] 的距離：
        //   j=0: 0
        //   j>0: new_L - j
        // 驗證：j=new_L-1 (最後一個點)，距離 1。new_L - (new_L-1) = 1。正確。
        for (int j = 0; j < new_L; ++j) {
            int u = new_cycle[j];
            int dist = (j == 0 ? 0 : new_L - j);
            W[u] = pow(K, dist) / denom;
        }

        // 用 BFS 計算非環點的 W 值
        // 反向圖：從環上的點開始，向外擴散
        // 對於非環點 v，W[v] = K * W[S[v]]? 
        // 不，公式 R(v) = Cv + K * sum(R(children))
        // 展開後 W[v] = K * W[S[v]]? 
        // 是的，因為 v 只有一條出路 S[v]。
        // 所以 W[v] = K^1 * W[S[v]]。
        // 我們可以拓撲排序（按距離環的遠近）
        // 用 queue，初始放入環上所有點
        vector<int> q;
        vector<bool> computed(N + 1, false);
        for (int u : new_cycle) {
            computed[u] = true;
            q.push_back(u);
        }

        int head = 0;
        while(head < q.size()){
            int u = q[head++];
            // u 的 W 已計算
            // 遍歷 u 的前驅 v (即 S[v] = u)
            for (int v : rev_adj[u]) {
                if (!computed[v] && !on_cycle[v]) {
                    // v 不在環上，且未計算
                    W[v] = K * W[u];
                    computed[v] = true;
                    q.push_back(v);
                }
            }
        }
        // 注意：原環上被甩出去的點（如 c_(i+1)）現在不在 new_cycle 中，但它们 S[v] 指向誰？
        // 在我們的設定中，除了 c_i 被改為指向 1，其他點的 S 不變。
        // 所以 c_(i+1) 的 S 依然是 c_(i+2)，最終 c_(L-1) 的 S 是 1 (new_cycle[0])。
        // 所以 BFS 會正確處理它們。

        double total_R = 0.0;
        for (int i = 1; i <= N; ++i) {
            total_R += C[i] * W[i];
        }

        // 計算額外修改的增益
        // 可修改點：除了 1 和 new_cycle.back() (即 c_i) 以外的所有點
        // 但注意：new_cycle.back() 已經被修改了，所以不能再改。
        // 其他點 v，如果改為指向 1，則新 W[v] = K / denom
        double W_direct = K / denom;
        vector<double> gains;
        int modified_node = new_cycle.back(); // c_i

        for (int v = 2; v <= N; ++v) { // v!=1
            if (v == modified_node) continue;
            double gain = C[v] * (W_direct - W[v]);
            if (gain > EPS) {
                gains.push_back(gain);
            }
        }

        sort(gains.rbegin(), gains.rend());
        int take = min(extra_moves, (int)gains.size());
        for (int i = 0; i < take; ++i) {
            total_R += gains[i];
        }

        return total_R;
    };

    double max_R = 0.0;

    if (M == 0) {
        // 不修改
        vector<int> original_cycle = cycle;
        max_R = calculate_R(original_cycle, L, 0);
    } else {
        // 枚舉將 cycle[i] 改為指向 1, i from 1 to L-1
        // new_cycle = [cycle[0], cycle[1], ..., cycle[i]]
        for (int i = 1; i < L; ++i) {
            vector<int> new_cycle;
            for (int j = 0; j <= i; ++j) {
                new_cycle.push_back(cycle[j]);
            }
            int new_L = i + 1;
            int extra = M - 1;
            double current_R = calculate_R(new_cycle, new_L, extra);
            if (current_R > max_R) {
                max_R = current_R;
            }
        }
    }

    cout << fixed << setprecision(2) << max_R << endl;

    return 0;
}
