        // 第一位數字可以是 1, 2, 3, 4。
        // 比 3 小的數字有 1, 2。
        // 如果第一位是 1：剩下從 {2, 3, 4} 選 2 個排列 -> P(3, 2) = 3*2 = 6 種。
        // 如果第一位是 2：剩下從 {1, 3, 4} 選 2 個排列 -> P(3, 2) = 6 種。
        // 目前累積 6 + 6 = 12。
        // 現在固定第一位是 3。剩餘可用數字 {1, 2, 4}。目標第二位是 4。
        // 在 {1, 2, 4} 中，比 4 小的有 1, 2。
        // 如果第二位是 1：剩下從 {2, 4} 選 1 個 -> P(2, 1) = 2 種。
        // 如果第二位是 2：剩下從 {1, 4} 選 1 個 -> P(2, 1) = 2 種。
        // 目前累積 12 + 2 + 2 = 16。
        // 現在固定前兩位是 3, 4。剩餘可用數字 {1, 2}。目標第三位是 2。
