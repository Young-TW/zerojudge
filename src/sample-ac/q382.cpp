        // 假設題目是：從 1 到 m 中選 n 個數做排列 (P(m, n))。
        // 範例 1: m=?, n=?。如果輸入是 "m n"，則 m=3, n=4 不可能。
        // 如果輸入是 "n m"，則 n=3, m=4。從 1~4 選 3 個做排列。
        // 給定的排列是 3, 4, 2。元素都在 1~4 之間。
        // 這樣就合理了！
        // 題目意涵：考慮從 1 到 m 中任選 n 個不同數字所組成的所有排列，並按字典序排序。
        // 給定其中一個排列，問它是第幾個？
        // 讓我們用這個假設驗證範例 1：
        // n=3, m=4。排列 3, 4, 2。
        // 所有可能的排列是從 {1, 2, 3, 4} 中選 3 個進行排列。總數 P(4, 3) = 4*3*2 = 24。
