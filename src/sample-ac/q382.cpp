        // 標準康托展開是計算 1~n 排列的字典序排名。
        // 公式：X = a[n]*(n-1)! + a[n-1]*(n-2)! + ... + a[1]*0!
        // 其中 a[i] 表示在位置 i 之後，比 p[i] 小的數字個數（且這些數字還沒被用過）。
        // 對於 3, 4, 2 (元素為 2, 3, 4)：
        // 排序後可用集合：{2, 3, 4}
        // 第 1 位是 3：比 3 小的有 {2}，共 1 個。貢獻 1 * 2! = 2。剩餘 {2, 4}
