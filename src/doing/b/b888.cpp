#include <iostream>

using namespace std;

int main() {
    long long a1, b1, c1;
    long long a2, b2, c2;
    long long a3, b3, c3;

    if (!(cin >> a1 >> b1 >> c1)) return 0;
    if (!(cin >> a2 >> b2 >> c2)) return 0;
    if (!(cin >> a3 >> b3 >> c3)) return 0;

    // 方程組：
    // a1*x + b1*y + z = c1  (可樂固定一杯，係數為 1)
    // a2*x + b2*y + z = c2
    // a3*x + b3*y + z = c3
    // 其中 x = RiceBurger, y = Cola, z = ChickenNugget
    // 注意題目說每人都只喝 1 杯可樂，所以可樂的係數都是 1，且包含在總花費 c1, c2, c3 中
    // 但輸出格式要求分別輸出 RiceBurger, Cola, ChickenNugget 的單價
    // 實際上題目敘述："所幸"OωO學長"一行人在前天的晚餐都只喝 1 杯可樂，大大地減少了"OωO學長"的計算過程"
    // 這暗示可樂是固定項。讓我們重新審視變數。
    // 設米漢堡單價 X, 可樂單價 Y, 雞塊單價 Z
    // a1*X + 1*Y + b1*Z = c1
    // a2*X + 1*Y + b2*Z = c2
    // a3*X + 1*Y + b3*Z = c3
    
    // 我們將方程組改寫為：
    // a1*X + b1*Z = c1 - Y
    // a2*X + b2*Z = c2 - Y
    // a3*X + b3*Z = c3 - Y
    // 這樣還是有四個變數 (X, Y, Z, 以及每個方程右边的 Y)。
    // 不對，應該是三個未知數 X, Y, Z。
    // 方程組為：
    // a1*X + 1*Y + b1*Z = c1
    // a2*X + 1*Y + b2*Z = c2
    // a3*X + 1*Y + b3*Z = c3
    
    // 利用高斯消去法或克拉瑪公式求解 3x3 線性方程組
    // 係數矩陣 A:
    // | a1  1  b1 |
    // | a2  1  b2 |
    // | a3  1  b3 |
    // 常數向量 B:
    // | c1 |
    // | c2 |
    // | c3 |
    // 未知數向量 X_vec:
    // | X |
    // | Y |
    // | Z |

    // 計算行列式 det(A)
    // det = a1*(1*b3 - b2*1) - 1*(a2*b3 - a3*b2) + b1*(a2*1 - a3*1)
    // det = a1*(b3 - b2) - (a2*b3 - a3*b2) + b1*(a2 - a3)
    
    long long det = a1 * (b3 - b2) - (a2 * b3 - a3 * b2) + b1 * (a2 - a3);

    long long total_spent = c1 + c2 + c3;
    long long snail_pay = 0; // T

    if (total_spent <= 480) {
        // 如果總花費小於等於 480 (160*3)，則老師給的 1000 元全退？
        // 題目說：snail 老師每餐只能補助 160 元，超過要自己墊錢。
        // 老師給了 1000 元。
        // 如果每人花費 <= 160，則剩餘金額歸老師？還是歸學生？
        // 題目描述："沒吃超過 160 元的那個人所剩下的錢並不能補給吃超過 160 元的那個人"
        // "OωO學長就開始祈禱 OωO學長一行人每人都吃超過 160 元，所以只要每人將差價補給 OωO學長，最後 OωO學長再給 snail 老師 1000-3×160 = 520 元就皆大歡喜了！"
        // 這暗示無論實際花費多少，老師只出 480 元 (160*3)。
        // 所以找錢 T = 1000 - min(c1, 160) - min(c2, 160) - min(c3, 160) ?
        // 不，題目情境是老師給了一張 1000 元，要買三人的晚餐。
        // 規則是每人補助上限 160。
        // 如果某人花了 c_i > 160，則該人需自付 (c_i - 160)。
        // 如果某人花了 c_i <= 160，則該人剩餘 (160 - c_i)，但這筆錢不能移轉。
        // 最終要還給老師的金額 T = 1000 - (三人實際花費總和) + (超過 160 部分自行吸收的金額？)
        // 讓我們重新解讀："snail 老師卻直接給 OωO學長 1 張 1000 元大鈔... 如果超過這個數目就要自己墊錢"
        // 這表示老師出的錢最多是 480 (160*3)。
        // 但老師給了 1000 元現金。
        // 結算時，學生們總共花了 S = c1+c2+c3。
        // 其中老師負擔的部分是 sum(min(ci, 160))。
        // 學生自付的部分是 sum(max(0LL, ci - 160))。
        // 老師給了 1000，實際花費 S。
        // 找回來的錢是 1000 - S。
        // 但這找回來的錢裡面，包含了老師多給的補助（如果有人沒吃滿 160）以及學生自付部分的零錢？
        // 不，邏輯應該是：
        // 總支出 S。
        // 老師應出 L = sum(min(ci, 160))。
        // 學生應出 P = sum(max(0LL, ci - 160))。
        // 驗證：L + P = sum(min(ci, 160) + max(0LL, ci - 160)) = sum(ci) = S。正確。
        // 現在手上有 1000 元鈔票。
        // 付款後找回 1000 - S。
        // 這筆找回的錢 (1000 - S) 應該全部還給老師嗎？
        // 題目範例 1: c1=35, c2=135, c3=80. Total = 250.
        // L = 35 + 135 + 80 = 250 (都小於 160). P = 0.
        // 找回 1000 - 250 = 750.
        // 範例輸出 Snail: 750.
        // 這表示找回的錢全數還給老師。
        // 再看另一種情況：假設有人超過 160。
        // 例如 c1=200, c2=100, c3=100. Total = 400.
        // L = 160 + 100 + 100 = 360. P = 40 + 0 + 0 = 40.
        // 學生需要自掏腰包 40 元。
        // 店家找回 1000 - 400 = 600 元。
        // 這 600 元中，有 360 元是老師的補助剩餘（因為老師原本該出 360，但給了 1000 中的份額？）
        // 不，老師給了 1000 元整。
        // 學生實際支付結構：老師出 360，學生出 40。
        // 但交易時是用那張 1000 元付的。
        // 找回 600 元。
        // 此時學生已經自付了 40 元（從口袋拿出來補足 1000 元不夠的部分？不對，1000 元夠付 400 元）。
        // 情境修正：老師給 1000 元。消費 400 元。找回 600 元。
        // 這 600 元怎麼分？
        // 老師實際應出 360 元。學生應出 40 元。
        // 但學生是用老師的 1000 元去付的。
        // 所以學生欠老師：(老師應出 360) - (學生已用老師的錢付掉的 360 部分) ... 這邏輯有點繞。
        // 簡單想：老師給了 1000。最後老師應該只支出 360。
        // 所以老師應該拿回 1000 - 360 = 640 元。
        // 但現在手上只有找回的 600 元。
        // 學生還需要再補 40 元給老師，湊成 640 元還給老師。
        // 這樣老師淨支出 1000 (給出) - 640 (收回) = 360。正確。
        // 學生淨支出 40 (補給老師)。正確。
        // 所以 T (還給老師的錢) = 1000 - L = 1000 - sum(min(ci, 160))。
        // 讓我們用範例 1 驗證：
        // c1=35, c2=135, c3=80. L = 35+135+80 = 250.
        // T = 1000 - 250 = 750. 符合範例輸出。
        // 所以 T 的計算公式確定為：T = 1000 - (min(c1, 160LL) + min(c2, 160LL) + min(c3, 160LL))。
        
        snail_pay = 1000 - (min(c1, 160LL) + min(c2, 160LL) + min(c3, 160LL));
    } else {
         // 即使總花費超過 480，公式依然成立嗎？
         // 例如 c1=200, c2=200, c3=200. Total = 600.
         // L = 160*3 = 480.
         // T = 1000 - 480 = 520.
         // 題目敘述："最後 OωO學長再給 snail 老師 1000-3×160 = 520 元就皆大歡喜了！"
         // 這確認了當大家都超過 160 時，T=520。
         // 公式 T = 1000 - sum(min(ci, 160)) 在此情況下也是 1000 - 480 = 520。
         // 所以公式通用。
         snail_pay = 1000 - (min(c1, 160LL) + min(c2, 160LL) + min(c3, 160LL));
    }

    if (det == 0) {
        // 行列式為 0，可能無解或無限多解
        // 需要檢查方程組是否一致
        // 由於題目保證唯一解時必為整數，且我們只需要區分 無解 vs 無限多解
        // 對於 3x3 系統，若 det=0，則 rank(A) < 3。
        // 我們可以使用增廣矩陣來判斷。
        // 或者更簡單地，嘗試解出其中兩個變數的關係，代入第三個檢查。
        // 但最穩健的方式是高斯消去。
        
        // 這裡手動模擬高斯消去判斷 rank(A) 和 rank(A|B)
        // 矩陣:
        // a1 1 b1 | c1
        // a2 1 b2 | c2
        // a3 1 b3 | c3
        
        // 由於第二行係數都是 1 (可樂)，我們可以兩兩相減消去 Y
        // Eq2 - Eq1: (a2-a1)X + (b2-b1)Z = c2-c1
        // Eq3 - Eq2: (a3-a2)X + (b3-b2)Z = c3-c2
        
        long long A_ = a2 - a1;
        long long B_ = b2 - b1;
        long long C_ = c2 - c1;
        
        long long D_ = a3 - a2;
        long long E_ = b3 - b2;
        long long F_ = c3 - c2;
        
        // 現在變成 2x2 系統：
        // A_ X + B_ Z = C_
        // D_ X + E_ Z = F_
        // 這個系統的行列式 det2 = A_*E_ - B_*D_
        // 注意原 3x3 的 det 其實等於這個 det2 (因為我們做了列運算消去 Y，且 Y 的係數列是 1,1,1)
        // 驗證：
        // det = a1(b3-b2) - (a2b3-a3b2) + b1(a2-a3)
        //     = a1(b3-b2) - a2b3 + a3b2 + b1a2 - b1a3
        // det2 = (a2-a1)(b3-b2) - (b2-b1)(a3-a2)
        //      = a2b3 - a2b2 - a1b3 + a1b2 - (b2a3 - b2a2 - b1a3 + b1a2)
        //      = a2b3 - a2b2 - a1b3 + a1b2 - b2a3 + b2a2 + b1a3 - b1a2
        //      = a2b3 - a1b3 + a1b2 - b2a3 + b1a3 - b1a2
        //      = b3(a2-a1) + b2(a1-a3) + a3b1 - a2b1 ? 
        // 讓我們重新整理 det:
        // det = a1b3 - a1b2 - a2b3 + a3b2 + a2b1 - a3b1
        // det2 = a2b3 - a2b2 - a1b3 + a1b2 - a3b2 + a2b2 + a3b1 - a2b1
        //      = a2b3 - a1b3 + a1b2 - a3b2 + a3b1 - a2b1
        //      = -(a1b3 - a2b3 - a1b2 + a3b2 - a3b1 + a2b1)
        //      = -(a1(b3-b2) - a2(b3-b1) ... ) 
        // 其實 det(3x3) 經過 Row2-Row1, Row3-Row2 後，展開會發現與 det2 相同（符號可能差異，取決於展開方式，但為 0 的條件一致）。
        // 既然 det(3x3) == 0，則 det2 也必為 0。
        // 所以 A_*E_ - B_*D_ == 0。
        
        // 現在檢查一致性：
        // 如果 A_*F_ != C_*D_ (交叉相乘檢查比例)，則無解。
        // 或者更精確地，檢查向量 (A_, B_, C_) 和 (D_, E_, F_) 是否平行。
        // 由於 det2 = 0，向量 (A_, B_) 和 (D_, E_) 平行。
        // 只需檢查 (A_, B_, C_) 和 (D_, E_, F_) 是否平行。
        // 即 A_*F_ == C_*D_ 且 B_*F_ == C_*E_ (處理 0 的情況要小心)
        // 更簡單的方法：如果 A_!=0 或 B_!=0，則看 C_ 是否符合比例。
        // 如果 A_=0 且 B_=0，則看 C_ 是否為 0。
        
        bool consistent = true;
        
        // 檢查第一組差分方程和第二組差分方程是否矛盾
        // A_ X + B_ Z = C_
        // D_ X + E_ Z = F_
        // 已知 A_*E_ == B_*D_
        
        // 情況 1: A_, B_, D_, E_ 全為 0
        if (A_ == 0 && B_ == 0 && D_ == 0 && E_ == 0) {
            if (C_ != 0 || F_ != 0) consistent = false;
        } 
        // 情況 2: 係數不全為 0，則必須滿足比例關係
        else {
            // 檢查 A_*F_ == C_*D_ 和 B_*F_ == C_*E_
            // 為了避免除以零，使用乘法
            if (A_ * F_ != C_ * D_) consistent = false;
            else if (B_ * F_ != C_ * E_) consistent = false;
            // 還需要檢查如果 A_,B_ 為 0 但 D_,E_ 不為 0 的情況？
            // 上面的乘法檢查涵蓋了。例如 A_=0, D_!=0 => 0 == C_*D_ => C_必須為 0。
        }
        
        if (!consistent) {
            cout << "Are you kidding me??" << endl;
        } else {
            cout << "ㄒoo many !! , Snail: " << snail_pay << endl;
        }
    } else {
        // 唯一解，使用克拉瑪公式或代數解
        // 我們已經有消去 Y 後的方程：
        // A_ X + B_ Z = C_
        // D_ X + E_ Z = F_
        // det2 = A_*E_ - B_*D_ (等於 det 或 -det，取絕對值或直接解)
        // 實際上 det(3x3) = det2 (經推導應為相等或相反，這裡直接用 det 變數)
        // 讓我們直接用克拉瑪公式解 X, Z
        // X = detX / det
        // Z = detZ / det
        // 其中 det 是原 3x3 矩陣的行列式
        
        // 重算 detX (替換第一行為常數項)
        // | c1  1  b1 |
        // | c2  1  b2 |
        // | c3  1  b3 |
        long long detX = c1 * (b3 - b2) - (c2 * b3 - c3 * b2) + b1 * (c2 - c3);
        
        // 重算 detY (替換第二行為常數項)
        // | a1  c1  b1 |
        // | a2  c2  b2 |
        // | a3  c3  b3 |
        long long detY = a1 * (c2 * b3 - c3 * b2) - c1 * (a2 * b3 - a3 * b2) + b1 * (a2 * c3 - a3 * c2);
        // 注意克拉瑪公式中，detY 的符號取決於位置，这里是 (+ - +)，所以中間項是 -c1 * minor
        // 上面的展開式：a1(...) - c1(...) + b1(...) 正確。
        
        // 重算 detZ (替換第三行為常數項)
        // | a1  1  c1 |
        // | a2  1  c2 |
        // | a3  1  c3 |
        long long detZ = a1 * (c3 - c2) - (a2 * c3 - a3 * c2) + c1 * (a2 - a3);
        
        // X = detX / det
        // Y = detY / det
        // Z = detZ / det
        
        long long X = detX / det;
        long long Y = detY / det;
        long long Z = detZ / det;
        
        cout << "RiceBurger: " << X << " , Cola: " << Y << " , ChickenNugget: " << Z << " , Snail: " << snail_pay << endl;
    }

    return 0;
}
