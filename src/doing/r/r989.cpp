#include <iostream>
#include <vector>
#include <algorithm>
#include <cmath>
#include <climits>

using namespace std;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int N, M;
    long long L;
    while (cin >> N >> M >> L) {
        vector<int> K(N);
        for (int i = 0; i < N; ++i) {
            cin >> K[i];
            --K[i]; // 轉為 0-based index
        }

        vector<long long> S(M);
        for (int i = 0; i < M; ++i) {
            cin >> S[i];
        }

        // 計算前綴和，以便快速計算區間和
        // prefix_sum[i] = S[0] + ... + S[i-1]
        // sum(S[a]...S[b]) = prefix_sum[b+1] - prefix_sum[a]
        // 注意題目定義 a <= b，且 S 的索引是 1-based 在題目描述中，但我們已轉為 0-based
        // 題目：音程 a, b (a<=b)，斥力 = floor((S_a + ... + S_b) / L)
        // 我們的 S 是 0-based，所以輸入的音程 k (1~M) 轉為 k-1 (0~M-1)
        // 若音程為 u, v (0-based, u<=v)，則和為 S[u] + ... + S[v]
        // 對應前綴和為 prefix_sum[v+1] - prefix_sum[u]
        
        vector<long long> prefix_sum(M + 1, 0);
        for (int i = 0; i < M; ++i) {
            prefix_sum[i + 1] = prefix_sum[i] + S[i];
        }

        // 計算任意兩個音程之間的斥力成本
        // cost[u][v] = floor((sum(S[u]...S[v])) / L), 其中 u <= v
        // 由於 N 可達 2000，我們不能預先計算所有 M*M 的成本 (M 可達 10^5)
        // 但我們只需要處理出現在 K 中的音程。
        // 實際上，我們需要的是對於給定的排列，相鄰元素的成本總和。
        // 這是一個 TSP (Traveling Salesman Problem) 的變體，但在圓環上。
        // 然而，N 可達 2000，一般的 TSP O(N^2 * 2^N) 或 O(N!) 是不可行的。
        // 必須觀察_cost_函數的性質。
        
        // 讓我們重新審視成本函數：
        // cost(a, b) = floor( (sum_S(min(a,b), max(a,b))) / L )
        // 這個成本函數滿足三角不等式嗎？不一定。
        // 但是，注意到成本只取決於兩個音程的值，以及它們之間的所有 S 值。
        // 如果我們將相同的音程放在一起會怎樣？
        // 或者，如果我們將音程排序後排列會怎樣？
        
        // 關鍵觀察：
        // 假設我們將音符按照音程值排序。
        // 考慮圓環上的相鄰對 (u, v)。成本是 sum_S(min(u,v), max(u,v)) / L。
        // 如果我們有一個排列 p_0, p_1, ..., p_{N-1}，總成本是 sum_{i=0}^{N-1} cost(p_i, p_{i+1}) (p_N = p_0)。
        // 對於任何一對 (u, v)，cost(u, v) 等於覆蓋從 min(u,v) 到 max(u,v) 的區間的 S 總和除以 L。
        // 想像這些音程點在一個數線上 (0 到 M-1)。
        // 當我們在圓環上從 u 走到 v，我們覆蓋了數線上的區間 [min(u,v), max(u,v)]。
        // 總成本是所有這些區間的覆蓋次數乘以對應的 S 值，再除以 L (向下取整是在每個區間計算後進行，這使問題變複雜)。
        // 等等，向下取整是在每個 pair 計算後進行，所以不能簡單地交換 sum 和 floor。
        // 但是，如果 L 很大，或者 S 的分佈特殊，可能有規律。
        
        // 讓我們再看一次範例 1:
        // N=4, M=5, L=99
        // K = {1, 4, 5, 3} -> 0-based: {0, 3, 4, 2}
        // S = {100, 200, 300, 400, 500}
        // 範例排列: 1, 3, 5, 4 (1-based) -> 0, 2, 4, 3
        // Pairs: (0,2), (2,4), (4,3), (3,0)
        // (0,2): sum(S[0]..S[2]) = 100+200+300 = 600. 600/99 = 6.06 -> 6
        // (2,4): sum(S[2]..S[4]) = 300+400+500 = 1200. 1200/99 = 12.12 -> 12
        // (4,3): min=3, max=4. sum(S[3]..S[4]) = 400+500 = 900. 900/99 = 9.09 -> 9
        // (3,0): min=0, max=3. sum(S[0]..S[3]) = 100+200+300+400 = 1000. 1000/99 = 10.10 -> 10
        // Total = 6+12+9+10 = 37. 正確。
        
        // 如果我們排序 K: 0, 2, 3, 4
        // 排列成圓環：0-2-3-4-0
        // Pairs: (0,2), (2,3), (3,4), (4,0)
        // (0,2): 600/99 = 6
        // (2,3): S[2]+S[3] = 300+400=700. 700/99 = 7
        // (3,4): S[3]+S[4] = 400+500=900. 900/99 = 9
        // (4,0): S[0]..S[4] = 1500. 1500/99 = 15
        // Total = 6+7+9+15 = 37. 一樣！
        
        // 另一種排序排列：0-3-2-4-0? 不太可能更好。
        // 直覺上，將相近的音程放在一起可以最小化區間長度，從而最小化和。
        // 特別是，如果我們將 K 排序為 k_0 <= k_1 <= ... <= k_{N-1}。
        // 然後將它們按順序放在圓環上：k_0, k_1, ..., k_{N-1}。
        // 相鄰對是 (k_i, k_{i+1}) 對於 i=0..N-2，以及 (k_{N-1}, k_0)。
        // 對於 (k_i, k_{i+1})，因為已排序，min=k_i, max=k_{i+1}。區間是 [k_i, k_{i+1}]。
        // 對於 (k_{N-1}, k_0)，min=k_0, max=k_{N-1}。區間是 [k_0, k_{N-1}]。
        // 這個區間 [k_0, k_{N-1}] 覆蓋了所有其他區間的併集嗎？
        // 實際上，數線上的每個基本區間 [j, j+1] (對應 S[j]) 被覆蓋了多少次？
        // 在路徑 k_0 -> k_1 -> ... -> k_{N-1} -> k_0 中：
        // 從 k_i 到 k_{i+1} (i < N-1)，我們覆蓋 [k_i, k_{i+1}]。
        // 從 k_{N-1} 到 k_0，我們覆蓋 [k_0, k_{N-1}]。
        // 考慮數線上的一點 x (代表 S[x])。它被覆蓋的次數等於有多少個相鄰對 (u,v) 使得 min(u,v) <= x <= max(u,v)。
        // 在排序後的序列中，對於 x 在 [k_0, k_{N-1}] 之間：
        // 每當路徑從 <=x 跳到 >x 或從 >x 跳到 <=x 時，就會覆蓋 x。
        // 在序列 k_0, k_1, ..., k_{N-1} 中，因為是單調遞增，只有一次從 <=x 到 >x 的跳躍（如果存在）。
        // 然後最後一步從 k_{N-1} (>x) 回到 k_0 (<=x)，又有一次跳躍。
        // 所以每個 x 在 [k_0, k_{N-1}] 範圍內恰好被覆蓋 2 次。
        // 如果 x < k_0 或 x > k_{N-1}，則被覆蓋 0 次。
        // 所以總和（在除以 L 和 floor 之前）是 2 * sum(S[k_0] ... S[k_{N-1}])。
        // 但是，因為有 floor 運算，sum(floor(...)) != floor(sum(...))。
        // 所以這種「覆蓋次數」的分析只能給出一個下界或近似值，不能精確計算。
        
        // 然而，是否有其他排列方式能得到更小的值？
        // 考慮如果不排序，例如交叉排列。這可能會導致某些區間被重複計算多次，而某些區間雖然單次成本高，但避免了巨大的區間。
        // 但注意到最大的區間總是 [min_K, max_K]，無論如何排列，只要 min_K 和 max_K 在圓環上相鄰，就會產生這個成本。
        // 如果它們不相鄰呢？例如 min_K ... some ... max_K ... some ... back to min_K。
        // 那麼從 min_K 到 max_K 的路徑有兩條：一條順時針，一條逆時針。
        // 在我們的成本定義中，cost(u,v) 總是取 [min(u,v), max(u,v)] 的和，也就是數線上的較短區間？不，是直接連接的區間。
        // 題目定義：音程 a, b (a<=b)，斥力 = sum(S[a]...S[b])/L。這總是數線上的區間，與圓環上的方向無關。
        // 所以 cost(u,v) 是對稱的，且總是對應數線上的閉區間 [min(u,v), max(u,v)]。
        // 因此，無論如何排列，如果我們有一對 (min_K, max_K)，成本就是 sum(S[min_K]...S[max_K])/L。
        // 如果我們避免讓 min_K 和 max_K 直接相鄰呢？
        // 例如：min_K, x, y, ..., max_K, z, ..., back to min_K。
        // 那麼我們有 (min_K, x), (x, y), ..., (y, max_K), (max_K, z), ..., (z, min_K)。
        // 區間 [min_K, max_K] 會被分解成多個小區間嗎？
        // cost(min_K, x) + cost(x, y) + ... + cost(y, max_K) 
        // = floor(Sum[min_K, x]/L) + floor(Sum[x, y]/L) + ... 
        // 這通常大於或等於 floor(Sum[min_K, max_K]/L)，因為 floor(a)+floor(b) <= floor(a+b) 是不成立的，實際上是 floor(a)+floor(b) <= floor(a+b) 是錯的，應該是 floor(a)+floor(b) <= floor(a+b) 嗎？
        // 例子：a=0.6, b=0.6. floor(a)=0, floor(b)=0, sum=0. floor(a+b)=floor(1.2)=1. 所以 0 <= 1.
        // 一般來說，floor(a) + floor(b) <= floor(a+b)。
        // 所以將大區間拆成小區間，其 floor 後的總和通常會小於或等於直接算大區間的 floor。
        // 等等，這意味著我們應該盡量拆分大區間？
        // 也就是說，避免直接連接距離遠的點，而是通過中間點逐步連接。
        // 這強烈暗示排序後的相鄰排列是最優的。
        // 因為如果我們排序後相連，我們得到的都是最小区間 [k_i, k_{i+1}]。
        // 唯一的大區間是 [k_0, k_{N-1}]，這是不可避免的，因為圓環必須封閉。
        // 任何其他排列都會引入一些「交叉」的邊，這些邊的區間可能會重疊，導致某些 S[j] 被計算多次，而且由於 floor 的非線性，很難說一定比較好。
        // 但根據 floor(a)+floor(b) <= floor(a+b) 的性質，拆分區間是有利的。
        // 所以策略應該是：將 K 排序，然後計算相鄰元素（包括首尾）的成本總和。
        
        // 讓我們驗證這個假設。
        // 假設 K = {0, 2, 4} (排序後)。S = {10, 10, 10, 10, 10}, L=15.
        // 排列 0-2-4-0:
        // (0,2): sum=20, 20/15=1.33->1
        // (2,4): sum=20, 20/15=1.33->1
        // (4,0): sum=50, 50/15=3.33->3
        // Total = 5.
        // 其他排列？只有這一種拓撲結構（圓環）。
        // 如果 K = {0, 1, 3, 4}. S={10,10,10,10,10}, L=15.
        // 排序：0,1,3,4.
        // (0,1): 10/15=0
        // (1,3): 20/15=1
        // (3,4): 10/15=0
        // (4,0): 50/15=3
        // Total = 4.
        // 如果排列成 0-3-1-4-0?
        // (0,3): 30/15=2
        // (3,1): 20/15=1
        // (1,4): 30/15=2
        // (4,0): 50/15=3
        // Total = 8. 更差。
        // 所以排序似乎是最優策略。
        
        sort(K.begin(), K.end());
        
        long long total_energy = 0;
        
        // 計算相鄰對 (K[i], K[i+1])
        for (int i = 0; i < N - 1; ++i) {
            int u = K[i];
            int v = K[i+1];
            // u <= v 因為已排序
            long long sum_val = prefix_sum[v + 1] - prefix_sum[u];
            total_energy += (sum_val / L);
        }
        
        // 計算最後一對 (K[N-1], K[0])
        int u = K[N - 1];
        int v = K[0];
        // min = v, max = u
        long long sum_val = prefix_sum[u + 1] - prefix_sum[v];
        total_energy += (sum_val / L);
        
        cout << total_energy << "\n";
    }

    return 0;
}
