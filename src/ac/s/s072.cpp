// 關鍵是：給定起始頂點 a 和目標頂點 b，以及三組軸
// 我們需要找到最少旋轉次數讓 a 轉到 b 的位置

// 簡化模型：
// 我們只關心「當前朝向觀察者的頂點」是誰
// 但旋轉操作會影響哪個頂點朝向觀察者，這取決於旋轉軸和當前狀態

// 更好的方法：BFS 狀態為 (當前朝向觀察者的頂點, 整個八面體的朝向)
// 但整個八面體的朝向太複雜

// 再簡化：
// 實際上，對於正八面體，從任意頂點出發，到其他頂點的最少旋轉次數是有規律的
// 考慮正八面體的幾何結構：
// - 每個頂點有 4 個相鄰頂點（距離 1 次旋轉可到？）
// - 1 個對跖點（相對頂點）

// 但旋轉操作是繞軸轉 90 度，不是直接移動頂點
// 讓我們枚舉所有可能性

// 實際解法：
// 由於 q 很大，我們需要 O(1) 或 O(log n) 回答每筆詢問
// 觀察：正八面體有 24 種旋轉對稱（旋轉群大小）
// 我們可以預計算所有 24 種狀態，然後對每筆測資建立映射

// 但每筆測資的軸定義不同，所以需要動態處理

// 另辟蹊徑：
// 直接 BFS，但狀態壓縮
// 狀態：目前哪個頂點在哪个位置（用 6! = 720 種排列）
// 720 很小，可以預計算所有可能旋轉下的最短路径

// 但軸的定義每筆測資不同，所以旋轉操作也不同
// 這意味著每筆測資的圖結構不同

// 等等，再讀題：
// v_x1, v_x2 定義 X 軸的兩個相對頂點
// 這表示在「初始配置」下，頂點 v_x1 和 v_x2 是相對的
// 旋轉是繞著這些軸進行的

// 關鍵洞察：
// 無論軸怎麼標號，正八面體的拓撲結構不變
// 從頂點 a 到頂點 b 的最少旋轉次數只取決於它們的相對位置關係：
// 1. a == b: 0 次
// 2. a 和 b 是相鄰頂點（共享一條邊）: ? 次
// 3. a 和 b 是相對頂點（穿過中心）: ? 次

// 在正八面體中：
// - 每個頂點有 1 個相對頂點
// - 每個頂點有 4 個相鄰頂點

