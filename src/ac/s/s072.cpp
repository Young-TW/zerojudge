// 初始狀態：位置 i 放著頂點 i
State initial_state() {
    State s;
    for (int i = 0; i < 6; ++i) s.pos[i] = i;
    return s;
}

// BFS 預計算所有狀態到初始狀態的最短距離
// 但問題是每筆測資的軸定義不同！
