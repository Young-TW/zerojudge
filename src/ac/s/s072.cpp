// 正八面體頂點編號 0~5
// 三個旋轉軸，每個軸由兩個相對頂點定義
// 每次旋轉 90 度，會改變頂點的位置

// 我們需要模擬正八面體的旋轉
// 正八面體有 6 個頂點，可以預先計算所有可能的狀態和最少步數

struct State {
    int pos[6]; // pos[i] = 原本在第 i 個位置的頂點現在在哪裡
    // 或者反過來：pos[i] = 現在在第 i 個位置的頂點原本是誰
    // 這裡我們定義：state[i] = 目前在位置 i 的頂點編號
};

// 檢查兩個狀態是否相同
bool same(const State& a, const State& b) {
    for (int i = 0; i < 6; ++i) {
        if (a.pos[i] != b.pos[i]) return false;
    }
    return true;
}

// 繞著通過頂點 axis1 和 axis2 的軸旋轉 90 度
// 需要知道哪些頂點會移動
// 正八面體：如果軸是 (0,1)，那麼其他四個頂點 (2,3,4,5) 會形成一個正方形，旋轉時循環移動
State rotate(const State& s, int axis1, int axis2, const vector<int>& perm) {
    State ns;
    // perm 定義了旋轉時四個非軸頂點的循環順序
    // 假設 perm = {a, b, c, d} 表示 a->b->c->d->a
    for (int i = 0; i < 6; ++i) {
        ns.pos[i] = s.pos[i];
    }
    // 找出非軸的四个位置
    vector<int> others;
    for (int i = 0; i < 6; ++i) {
        if (i != axis1 && i != axis2) {
            others.push_back(i);
        }
    }
    // 根據 perm 進行循環移位
    // perm 給出的是位置的循環順序
    int temp = ns.pos[others[perm[3]]];
    ns.pos[others[perm[3]]] = ns.pos[others[perm[2]]];
    ns.pos[others[perm[2]]] = ns.pos[others[perm[1]]];
    ns.pos[others[perm[1]]] = ns.pos[others[perm[0]]];
    ns.pos[others[perm[0]]] = temp;
    
    return ns;
}

// 更簡單的方法：直接硬編碼旋轉
