#include <iostream>
#include <vector>
#include <numeric>
#include <algorithm>
#include <cstring>

using namespace std;

const int MAX_SUM = 700005;

void solve() {
    int n, q, x;
    if (!(cin >> n >> q >> x)) return;

    vector<int> w(n + 1);
    long long total_sum = 0;
    for (int i = 1; i <= n; ++i) {
        cin >> w[i];
        total_sum += w[i];
    }

    // dp[s] = 是否可以用子集組成和為 s
    // 為了處理「優先選較大的植物」，我們需要按照特定順序考慮物品
    // 題目策略：
    // 1. 優先給愈高的植物愈好 (w_i 大的優先)
    // 2. 若 w_i 相同，優先選溫室玻璃罩高度比較小的 (i 小的優先)
    
    // 所以排序順序：先按 w_i 降序，若 w_i 相同則按 i 升序
    vector<pair<int, int>> plants;
    for (int i = 1; i <= n; ++i) {
        plants.push_back({w[i], i});
    }
    
    sort(plants.begin(), plants.end(), [](const pair<int, int>& a, const pair<int, int>& b) {
        if (a.first != b.first) {
            return a.first > b.first; // w_i 大的在前
        }
        return a.second < b.second; // w_i 相同時，i 小的在前
    });

    // dp[s] = 是否能組成和為 s
    vector<bool> dp(total_sum + 1, false);
    dp[0] = true;
    
    // 為了判斷是否必須使用第 x 個溫室的植物，我們需要知道：
    // 1. 不使用該植物能否組成 H
    // 2. 使用該植物能否組成 H
    
    // 但題目要求的是「在貪心選擇下」是否會選到第 x 個植物
    // 這意味著我們需要模擬貪心過程
    
    // 重新理解題目：
    // 對於每個查詢 H，我們要用貪心策略選擇植物：
    // - 從大到小考慮植物（按 w_i 降序，w_i 相同則 i 升序）
    // - 如果當前植物可以加入（不超過 H），就加入
    // - 最後看是否剛好等於 H，以及是否包含第 x 個植物
    
    // 但這不是標準的背包問題，而是貪心選擇
    // 讓我們重新思考...
    
    // 實際上，題目描述的是一種特定的選擇策略：
    // 對於目標 H，我們按照排序後的順序，盡可能選擇植物
    // 如果當前植物的重量 <= 剩餘需要的重量，就選擇它
    
    // 這種貪心策略不一定能得到最优解，但題目明確說這就是農夫的策略
    
    // 所以對於每個查詢，我們直接模擬貪心過程即可
    // 但這樣複雜度是 O(q * n)，對於 q=10^5, n=10^3，總共 10^8 操作，可能超時
    
    // 我們需要優化。觀察到：
    // 貪心選擇的結果只取決於 H 和植物的排序
    // 我們可以預處理所有可能的 H 的結果嗎？
    
    // 另一個思路：由於植物已經排序，對於每個 H，貪心選擇是確定的
    // 我們可以預計算每個 H 是否可達，以及是否包含第 x 個植物
    
    // 但 H 最大可到 7*10^5，而總和可能更大，但題目說 H_i <= 7*10^5
    // 所以我們只需要考慮 H <= 700000
    
    // 讓我們用 DP 來模擬貪心過程：
    // dp[s] = 在貪心策略下，組成和為 s 時是否使用了第 x 個植物
    // 但貪心策略不是標準的子集和，它依賴於選擇順序
    
    // 正確的做法：
    // 由於貪心策略是確定的（按固定順序選擇），我們可以模擬這個過程
    // 對於每個可能的和 s，記錄：
    // - 是否可以通過貪心策略達到 s
    // - 如果可以，是否使用了第 x 個植物
    
    // 但貪心策略下，對於同一個和 s，可能有不同的選擇路徑嗎？
    // 不會，因為選擇順序是固定的，對於給定的 H，貪心選擇是唯一確定的
    
    // 等等，我誤解了。貪心策略是：
    // 給定目標 H，從第一個植物開始，如果 w <= H，就選它，然後 H -= w
    // 繼續下一個植物，直到 H = 0 或沒有更多植物可選
    
    // 這意味著對於不同的 H，選擇的植物集合不同
    // 我們不能簡單地用標準背包 DP
    
    // 但我們可以這樣做：
    // 預處理：對於每個可能的起始剩餘值 H，模擬貪心過程
    // 但這還是 O(MAX_H * n)
    
    // 讓我們換個角度：
    // 定義 f[H] = 在貪心策略下，針對目標 H，最終選擇的植物集合的和
    // 如果 f[H] == H，表示可以剛好組成 H
    // 同時記錄 g[H] = 是否在第 x 個植物被選擇
    
    // 但即使這樣，計算 f[H] 仍需 O(n) 時間
    
    // 觀察貪心過程的性質：
    // 當我們按順序考慮植物時，對於目標 H：
    // - 如果 w[0] <= H，選擇 w[0]，然後解決子問題 H - w[0]
    // - 否則，跳過 w[0]，解決子問題 H
    
    // 這提示我們可以用 DP：
    // dp[i][s] = 考慮前 i 個植物，目標為 s 時，貪心選擇的結果（實際組成的和）
    // 但狀態空間太大
    
    // 另一個觀察：
    // 在貪心策略下，對於目標 H，選擇的植物集合是唯一確定的
    // 設 chosen(H) 為選擇的植物索引集合
    // sum(chosen(H)) 可能小於 H（如果無法剛好組成）
    
    // 關鍵洞察：
    // 如果我們按排序後的順序處理植物，並維護一個集合 S 表示「可以組成的和」
    // 但這不是標準子集和，因為貪心策略限制了選擇方式
    
    // 讓我們回到基礎，直接模擬每個查詢：
    // 對於每個查詢 H，模擬貪心選擇過程
    // 複雜度 O(q * n) = 10^5 * 10^3 = 10^8
    // 在 C++ 中，10^8 次簡單操作通常可以在 1-2 秒內完成
    // 考慮到有 T 筆測資，可能需要優化
    
    // 但題目說 30% 測資 q <= 1000，其他可能 q 很大
    // 讓我們嘗試直接模擬，看看是否能通過
    
    // 首先找到第 x 個植物在排序後的位置
    int x_plant_index = -1;
    for (int i = 0; i < n; ++i) {
        if (plants[i].second == x) {
            x_plant_index = i;
            break;
        }
    }
    
    int A = 0, B = 0, C = 0;
    
    for (int k = 0; k < q; ++k) {
        int H;
        cin >> H;
        
        if (H > total_sum) {
            A++;
            continue;
        }
        
        int remaining = H;
        bool use_x = false;
        
        // 模擬貪心選擇
        for (int i = 0; i < n && remaining > 0; ++i) {
            if (plants[i].first <= remaining) {
                remaining -= plants[i].first;
                if (i == x_plant_index) {
                    use_x = true;
                }
            }
        }
        
        if (remaining != 0) {
            A++;
        } else if (use_x) {
            B++;
        } else {
            C++;
        }
    }
    
    cout << A << " " << B << " " << C << "\n";
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    
    int T;
    if (cin >> T) {
        while (T--) {
            solve();
        }
    }
    
    return 0;
}
